/**
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 🧠 【今日学ぶこと】
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 *
 * Step6-02では「required（リクワイアード）」「string（ストリング）」
 * 「integer（インテジャー）」「max（マックス）」「min（ミン）」を学びました。
 *
 * でも、Laravelにはこれ以外にもたくさんのルールが用意されています。
 * このStepでは「よく使うルール」を中心に学んでいきます。
 *
 * 【バリデーションルールとは？】
 * バリデーション（Validation）＝「入力チェック」の意味。
 * ルール（Rule）＝「決まり」の意味。
 *
 * つまり「入力された値がこの決まりに従っているか確認する」仕組みです。
 *
 * 【たとえ話】
 * 入学願書を想像してください。
 * - 「名前は必ず書いてください」→ required（必須）
 * - 「メールアドレスの形式で書いてください」→ email（メール形式）
 * - 「生年月日は日付の形式で書いてください」→ date（日付形式）
 * - 「学年は1〜6の中から選んでください」→ in（指定された値のみ）
 * +-
 * このように、入力する人にルールを守ってもらうのがバリデーションです。
 *
 * 【今日学ぶルール一覧】
 * 1. email（イーメール）= メールアドレスの形式かチェック
 * 2. unique（ユニーク）= データベースに同じ値がないかチェック
 * 3. confirmed（コンファームド）= 確認用フィールドと一致するかチェック
 * 4. date（デート）= 日付の形式かチェック
 * 5. in（イン）= 指定した値の中に含まれているかチェック
 * 6. regex（レジェックス）= 正規表現でのパターンチェック
 * 7. nullable（ヌラブル）= 空欄を許可する
 * 8. sometimes（サムタイムズ）= 入力された場合のみチェック
 */

/**
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * ✅ 【前提の確認：ルールの書き方の復習】
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 *
 * Step6-02で学んだ基本の書き方を確認しましょう。
 *
 * $request->validate([
 *     'フィールド名' => 'ルール1|ルール2|ルール3',
 * ]);
 *
 * 【ポイント】
 * - |（パイプ）でルールを複数つなげられる
 * - 'category' => 'required|string|max:50'
 *   → 「必須 かつ 文字列 かつ 最大50文字」という意味
 *
 * この書き方で、今日学ぶルールも使えます。
 */

/**
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * ✅ 【1. email - メールアドレスの形式チェック】
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 *
 * email（イーメール） = 「メールアドレスの形式で入力してください」というルール
 *
 * 【たとえ話】
 * ハガキの宛先を書くとき「〇〇市△△町1-2-3」という住所の形式で
 * 書かないと届かないですよね。
 * email ルールは「@が含まれているか」「ドットが正しい位置にあるか」
 * などをチェックしてくれます。
 *
 * 【合格する値の例】
 * ○ yamada@example.com
 * ○ test.user@gmail.com
 *
 * 【不合格になる値の例】
 * × yamada  （@がない）
 * × @example.com  （ユーザー名がない）
 * × yamada@  （ドメインがない）
 *
 * 【コードの書き方】
 * 'email' => 'required|email'
 *
 * 【コード分解】
 * required = 「必須入力」
 * | = 「かつ」
 * email = 「メールアドレスの形式」
 *
 * 【実際の使い道】
 * - ユーザー登録フォーム
 * - お問い合わせフォーム
 * - メールマガジン登録フォーム
 */

