/**
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * ✅ 【2. unique - データベースに同じ値がないかチェック】
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 *
 * unique（ユニーク） = 「データベースにすでに同じ値が登録されていないか」
 *                      チェックするルール。「ユニーク」=「唯一の」という意味。
 *
 * 【たとえ話】
 * マイナンバーカードを想像してください。
 * 同じマイナンバーを持つ人が2人いたら困りますよね。
 * unique ルールは「この値はまだ誰も使っていないか？」を確認してくれます。
 *
 * 【書き方】
 * 'email' => 'required|email|unique:テーブル名,カラム名'
 *
 * 例：
 * 'email' => 'required|email|unique:users,email'
 *
 * 【コード分解】
 * unique = 「重複なし」というルール
 * : = 「条件の指定」
 * users = 「usersテーブルで調べる」
 * , = 「区切り」
 * email = 「emailカラムを調べる」
 *
 * 【注意点：編集時の問題】
 * ユーザー情報を「更新（編集）」するとき、
 * 「自分自身のメールアドレス」は重複OKにしないといけません。
 *
 * そうしないと「既存のメールアドレスと同じ」というエラーになってしまいます。
 * （自分自身のメールアドレスなのに！）
 *
 * 解決方法：ignore（イグノア）＝「無視する」を使います。
 * 'email' => 'required|email|unique:users,email,' . $user->id
 *
 * この書き方で「このユーザーID以外で重複チェックしてね」という意味になります。
 * ※ Step6-05（FormRequest）でより整理した書き方を学びます。
 *
 * 【実際の使い道】
 * - メールアドレス（同じメールで2人登録させない）
 * - ユーザー名（同じ名前が重複しないように）
 * - 商品コード（同じコードの商品が2つできないように）
 */

/**
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * ✅ 【3. confirmed - 確認用フィールドとの一致チェック】
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 *
 * confirmed（コンファームド） = 「確認用フィールドと一致しているか」
 *                               チェックするルール。
 *                              「confirm」=「確認する」という意味。
 *
 * 【たとえ話】
 * 重要な書類にサインするとき「もう一度ここにもサインしてください」と
 * 言われることがあります。2回同じことをさせることで「間違いがないか確認」します。
 * confirmed ルールはパスワードの「2回入力」でよく使われます。
 *
 * 【Laravelの仕組み】
 * confirmed を使うと、Laravelは自動的に
 * 「フィールド名_confirmation（コンファメーション）」という名前の
 * フィールドと一致するかチェックします。
 *
 * 例：
 * 'password' => 'required|confirmed'
 * → 「password」と「password_confirmation」が一致するかチェック
 *
 * 【Bladeフォームの書き方】
 * <input type="password" name="password">
 * <input type="password" name="password_confirmation">  ← この名前が大事！
 *
 * 【注意点】
 * - password_confirmation というフィールド名は変更できません（Laravelの決まり）
 * - 大文字小文字も一致しないとNGです
 *
 * 【実際の使い道】
 * - 新規ユーザー登録のパスワード入力
 * - パスワード変更フォーム
 * - 重要な値の確認入力
 */

/**
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * ✅ 【4. date / date_format - 日付のチェック】
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 *
 * date（デート） = 「日付の形式（フォーマット）かどうか」チェックするルール
 *
 * 【たとえ話】
 * カレンダーに日付を書くとき「2026/03/09」のように
 * 正しい形式で書かないと「何日のこと？」とわからなくなりますよね。
 * date ルールは「これは正しい日付の形式か？」を確認します。
 *
 * 【合格する値の例】
 * ○ 2026-03-09
 * ○ 2026/03/09
 * ○ March 9, 2026
 *
 * 【不合格になる値の例】
 * × abc  （日付ではない）
 * × 2026-99-99  （存在しない日付）
 *
 * 【date_format - より厳密な形式指定】
 * date_format（デートフォーマット） = 「この形式の日付だけOK」というルール
 *
 * 例：
 * 'date_field' => 'required|date_format:Y-m-d'
 * → 「2026-03-09」のような形式だけOK
 *
 * 【形式の記号の意味】
 * Y = 4桁の年（2026）
 * m = 2桁の月（03）
 * d = 2桁の日（09）
 *
 * 【after / before - 日付の前後チェック】
 * after（アフター）= 「この日付より後か」チェック
 * before（ビフォー）= 「この日付より前か」チェック
 *
 * 例：
 * 'start_date' => 'required|date|after:today'
 * → 「今日より後の日付を入力してください」という意味
 *
 * 'end_date' => 'required|date|after:start_date'
 * → 「終了日は開始日より後にしてください」という意味
 *
 * 【実際の使い道】
 * - 支出日の入力（家計簿アプリ）
 * - 予約フォームの日付選択
 * - 生年月日の入力
 */

/**
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * ✅ 【5. in - 指定した値のどれかであるかチェック】
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 *
 * in（イン） = 「指定したリストの中に含まれているか」チェックするルール
 *
 * 【たとえ話】
 * アンケートの「性別」の選択肢を想像してください。
 * 「男性・女性・その他」の3つから選ぶフォームなら、
 * それ以外の値（例：「犬」）を入力されたら困りますよね。
 * in ルールは「この選択肢の中から選んでください」を強制します。
 *
 * 【書き方】
 * 'category' => 'required|in:食費,交通費,娯楽費,その他'
 *
 * 【コード分解】
 * in = 「この中に含まれているか」チェック
 * : = 「条件の指定」
 * 食費,交通費,娯楽費,その他 = 「許可する値のリスト」（カンマで区切る）
 *
 * 【not_in - 逆の指定】
 * not_in（ノットイン） = 「このリストの中に含まれていないか」チェック
 *
 * 例：
 * 'username' => 'required|not_in:admin,root,system'
 * → 「admin・root・systemという名前は使えません」という意味
 *
 * 【注意点】
 * セレクトボックス（プルダウン）から選ばせる場合でも、
 * サーバー側でも in チェックをすることが大切です。
 * HTML の改ざん（かいざん）でおかしな値を送ることもできてしまうためです。
 *
 * 【実際の使い道】
 * - カテゴリの選択（食費・交通費・娯楽費）
 * - 支払い方法の選択（現金・クレジットカード）
 * - ステータスの選択（未払い・支払済み）
 */

/**
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * ✅ 【6. regex - 正規表現でのパターンチェック】
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 *
 * regex（レジェックス） = 「正規表現（せいきひょうげん）というパターンに
 *                          一致しているか」チェックするルール
 *
 * 【正規表現とは？】
 * 正規表現（せいきひょうげん）= 文字のパターンを表す特別な書き方。
 * 「数字3桁のあとにハイフン、さらに数字4桁」のような
 * 複雑なパターンを表現できます。
 *
 * 【たとえ話】
 * 郵便番号「123-4567」のように「数字3桁-数字4桁」という決まった形があります。
 * regex ルールはこういった「特定のパターン」での入力をチェックできます。
 *
 * 【よく使うパターン例】
 *
 * 郵便番号（〒123-4567 の形式）：
 * 'zip_code' => ['required', 'regex:/^\d{3}-\d{4}$/']
 *
 * 電話番号（数字とハイフンのみ）：
 * 'phone' => ['required', 'regex:/^[0-9\-]+$/']
 *
 * 【記号の読み方】
 * / = 正規表現の開始と終了を示す記号
 * ^ = 「文字列の先頭」
 * $ = 「文字列の末尾」
 * \d = 「数字（0〜9）1文字」
 * {3} = 「直前のパターンが3回」
 * - = 「ハイフン記号そのもの」
 *
 * 【注意点】
 * - regex を使うとき、| と混在するとうまく動かないことがあります
 * - そのため、配列形式 [] でルールを書くのが安全です
 *
 * 【配列形式の書き方（配列のルール指定）】
 * 通常の書き方：
 * 'field' => 'required|string'
 *
 * 配列の書き方：
 * 'field' => ['required', 'string']
 *
 * どちらも同じ意味です。regex を使うときは配列形式が推奨されています。
 *
 * 【実際の使い道】
 * - 郵便番号の形式チェック
 * - 電話番号の形式チェック
 * - 特定フォーマットのコード入力
 */

/**
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * ✅ 【7. nullable - 空欄（null）を許可する】
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 *
 * nullable（ヌラブル） = 「空欄（かららん）でもOK」というルール
 *                       「null（ヌル）」= 「何もない・空っぽ」という意味
 *
 * 【たとえ話】
 * アンケートで「備考欄（自由記入）」は、書かなくてもいい項目ですよね。
 * nullable はそういった「任意入力（にゅうりょく）の項目」に使います。
 *
 * 【required との違い】
 * required = 「必ず入力してください」（空欄NG）
 * nullable = 「空欄でもOKです」
 *
 * 【よくある使い方】
 * 空欄でもいいけど、入力するなら正しい形式で：
 * 'memo' => 'nullable|string|max:200'
 * → 「メモは空欄OKだけど、書くなら文字列で最大200文字まで」
 *
 * 'url' => 'nullable|url'
 * → 「URLは空欄OKだけど、書くなら正しいURL形式で」
 *
 * 【注意点】
 * nullable をつけないで、required もつけない場合：
 * → 空欄を送ると「The field must be a string.」などのエラーになることがある
 * → 任意入力の項目には必ず nullable をつける習慣をつけましょう
 *
 * 【実際の使い道】
 * - メモ・備考欄
 * - 任意入力のプロフィール欄（電話番号、ウェブサイトなど）
 * - オプション項目
 */

/**
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * ✅ 【8. sometimes - 入力された場合のみチェック】
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 *
 * sometimes（サムタイムズ） = 「フィールドが送信された場合にだけチェックする」
 *                            「some（サム）」=「いくつかの・ときどき」
 *
 * 【nullable との違い】
 * nullable = フィールドが空欄（null）でもOK
 * sometimes = フィールド自体が存在しない（送信されない）場合もOK
 *
 * 【たとえ話】
 * アンケートで「希望連絡先を選んでください（メール / 電話）」という質問があり、
 * 「電話」を選んだ人だけ「電話番号」を入力する、というフォームを想像してください。
 * sometimes はこういった「場合によって送信されないフィールド」に使います。
 *
 * 【実際の使い道】
 * - 部分更新（一部のフィールドだけ更新するフォーム）
 * - 条件付きフォーム（選択によって表示が変わる項目）
 *
 * 【注意点】
 * 通常の開発では nullable の方がよく使います。
 * sometimes は少し複雑なフォームで使うことが多いです。
 */

/**
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * ✅ 【9. 実践：ExpenseControllerのバリデーションを拡張する】
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 *
 * Step6-02で作成した ExpenseController の store() メソッドを
 * より充実したバリデーションに改善してみましょう。
 *
 * 【現在のバリデーション（Step6-02の状態）】
 *
 * $request->validate([
 *     'category'    => 'required|string|max:50',
 *     'amount'      => 'required|integer|min:1',
 *     'description' => 'nullable|string|max:200',
 *     'spent_at'    => 'required|date',
 * ]);
 *
 * 【改善版：今日学んだルールを追加してみよう】
 *
 * $request->validate([
 *     'category' => 'required|in:食費,交通費,娯楽費,その他',
 *     'amount'   => 'required|integer|min:1|max:9999999',
 *     'description' => 'nullable|string|max:200',
 *     'spent_at'    => 'required|date|before_or_equal:today',
 * ]);
 *
 * 【改善ポイントの解説】
 * category：
 *   before = 'required|string|max:50'
 *   after  = 'required|in:食費,交通費,娯楽費,その他'
 *   理由：自由入力より選択式にすることでデータの統一性が高まる
 *
 * amount：
 *   before = 'required|integer|min:1'
 *   after  = 'required|integer|min:1|max:9999999'
 *   理由：異常に大きな金額を弾く（max は任意）
 *
 * spent_at：
 *   before = 'required|date'
 *   after  = 'required|date|before_or_equal:today'
 *   理由：未来の日付を弾く（家計簿なので未来の支出はおかしい）
 *
 * 【before_or_equal（ビフォーオアイコール）とは？】
 * before（ビフォー） = 「この日付より前」
 * before_or_equal（ビフォーオアイコール） = 「この日付以前（当日もOK）」
 * today = 「今日」を表す特別なキーワード
 *
 * 同様に after_or_equal（アフターオアイコール）= 「この日付以降」もあります。
 */

/**
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * ✅ 【10. ルール一覧まとめ】
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 *
 * 【これまでに学んだルール（Step6-02 + Step6-04）】
 *
 * ■ 基本（Step6-02で学習済み）
 * required（リクワイアード）= 必須入力
 * string（ストリング）     = 文字列
 * integer（インテジャー）  = 整数（数字）
 * max:数値（マックス）     = 最大値・最大文字数
 * min:数値（ミン）         = 最小値・最小文字数
 *
 * ■ フォーマット系（Step6-04で学習）
 * email（イーメール）      = メールアドレスの形式
 * url（ユーアールエル）    = URLの形式
 * date（デート）           = 日付の形式
 * date_format:形式（デートフォーマット）= 指定の日付形式
 * regex:パターン（レジェックス）= 正規表現パターン
 *
 * ■ データベース系（Step6-04で学習）
 * unique:テーブル,カラム（ユニーク）= データベースに重複なし
 *
 * ■ 一致・比較系（Step6-04で学習）
 * confirmed（コンファームド）      = 確認フィールドと一致
 * in:値1,値2（イン）               = 指定した値のいずれか
 * not_in:値1,値2（ノットイン）     = 指定した値以外
 * after:日付（アフター）           = 指定日付より後
 * before:日付（ビフォー）          = 指定日付より前
 * after_or_equal:日付             = 指定日付以降
 * before_or_equal:日付            = 指定日付以前
 *
 * ■ 必須・任意系（Step6-04で学習）
 * nullable（ヌラブル）      = 空欄OK（null許可）
 * sometimes（サムタイムズ） = フィールドが存在する場合のみチェック
 *
 * ※ Laravelには他にも100種類以上のルールがあります。
 *   公式ドキュメント（えいご）で一覧を確認できます。
 *   https://laravel.com/docs/10.x/validation#available-validation-rules
 */

/**
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * ✅ 【よくある質問（Q&A）】
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 *
 * Q1. email ルールはメールが実際に存在するかまで確認しますか？
 * A1. しません。email ルールは「形式」だけを確認します。
 *     「@が含まれているか」「ドメイン部分があるか」などのチェックです。
 *     「このメールアドレスに本当にメールが届くか」は確認しません。
 *     そのため、「tes@t@example.com」のようなおかしな形式はNGですが、
 *     「nonexistent@fakdomain.com」のような存在しないアドレスはOKになります。
 *
 * Q2. unique を使うとき、カラム名を省略できますか？
 * A2. できます。フィールド名とカラム名が同じ場合は省略可能です。
 *     'email' => 'unique:users'  ← emailカラムをチェック（省略形）
 *     'email' => 'unique:users,email'  ← 明示的な書き方
 *     どちらも同じ意味になります。明示的な書き方の方が分かりやすいです。
 *
 * Q3. nullable と required は同時に使えますか？
 * A3. 使えません。矛盾しています。
 *     required = 「必ず入力してください」
 *     nullable = 「空欄でもOK」
 *     どちらか一方を使います。
 *     任意入力 → nullable
 *     必須入力 → required
 *
 * Q4. in のリストが多い場合（10個以上）はどうすればいい？
 * A4. Rule::in（ルールイン）を使うのが良いです。
 *     これは Step6-05（FormRequest）で学ぶ内容ですが、
 *     配列でリストを管理できるため、大量の選択肢でも見やすくなります。
 *
 *     例：
 *     use Illuminate\Validation\Rule;
 *
 *     'category' => [
 *         'required',
 *         Rule::in(['食費', '交通費', '娯楽費', '医療費', '光熱費', 'その他']),
 *     ]
 *
 * Q5. regex は難しそうで、使う機会はありますか？
 * A5. 郵便番号・電話番号・パスワード強度チェックなど、
 *     特定の形式を強制したい場面で使います。
 *     日常的にはあまり使わないので、「こういうものがある」という
 *     認識で大丈夫です。必要になったときに調べればOKです。
 *
 * Q6. date_format と date の使い分けは？
 * A6. date = 「何らかの日付形式であればOK」（緩やか）
 *     date_format = 「この特定の形式でないとNG」（厳密）
 *
 *     HTML の <input type="date"> は「2026-03-09」の形式で送信されます。
 *     そのため 'date_format:Y-m-d' と合わせて使うとより確実です。
 *
 *     ただし、日本語サイトで日本の日付形式（2026年3月9日）を受け付けたい場合は
 *     カスタムバリデーションが必要になります（Step6-06で触れます）。
 */

/**
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 📚 【用語集】
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 *
 * email（イーメール）
 * └─ メールアドレスの形式かチェックするルール
 *    「@が入っているか」「ドメイン部分があるか」などを確認する
 *
 * unique（ユニーク）
 * └─ 「唯一の」という意味。データベースに同じ値がないかチェックするルール
 *    重複登録を防ぐために使う
 *
 * confirmed（コンファームド）
 * └─ 「確認された」という意味。確認用フィールドと一致するかチェック
 *    パスワードの「2回入力」でよく使う
 *
 * password_confirmation（パスワードコンファメーション）
 * └─ confirmed ルールを使ったとき、Laravelが自動的に照合する
 *    「フィールド名_confirmation」という名前のフィールド
 *
 * date（デート）
 * └─ 日付の形式かチェックするルール
 *
 * date_format（デートフォーマット）
 * └─ 指定した日付の形式（例：Y-m-d）かチェックするルール
 *
 * after（アフター）/ before（ビフォー）
 * └─ 日付の前後をチェックするルール。after:today で「今日より後」
 *
 * before_or_equal（ビフォーオアイコール）
 * └─ 「指定日以前」チェック。before_or_equal:today で「今日以前」
 *
 * in（イン）
 * └─ 「〜の中に」という意味。指定した値のどれかであるかチェック
 *
 * not_in（ノットイン）
 * └─ 「〜の中に含まれていない」チェック。指定した値以外かどうか確認
 *
 * regex（レジェックス）
 * └─ 正規表現（Regular Expression）の略。文字のパターンチェックに使う
 *
 * nullable（ヌラブル）
 * └─ null（ヌル＝空っぽ）を許可するルール。任意入力の項目に使う
 *
 * sometimes（サムタイムズ）
 * └─ フィールドが送信された場合にだけチェックするルール
 *
 * Rule::in（ルール イン）
 * └─ Ruleクラスを使ったin指定。配列で選択肢を書けるので大量の選択肢に便利
 */

