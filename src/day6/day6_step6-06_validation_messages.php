/**
 * 📘 Day6 教材（Step6-06：カスタムエラーメッセージと日本語化）
 *
 * この教材では「バリデーションのエラーメッセージを日本語にする方法」を学びます
 */

/**
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 🧠 【今日学ぶこと】
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 *
 * Step6-05でFormRequestを使ってバリデーションを切り出しました。
 * そのとき、messages()（メッセージズ）を使って個別に日本語のエラーメッセージを設定しました。
 *
 * 【Step6-05のおさらい】
 * public function messages()
 * {
 *     return [
 *         'category.required' => 'カテゴリーを入力してください。',
 *         'amount.required'   => '金額を入力してください。',
 *     ];
 * }
 *
 * この書き方には1つ問題があります。
 * 「フィールドごとに1つ1つ日本語メッセージを書く必要がある」ということです。
 * フィールドが10個、20個と増えると、messages()がどんどん長くなってしまいます。
 *
 * 【今日学ぶ解決策】
 * Laravelには「言語ファイル（げんごふぁいる）」という仕組みがあります。
 * アプリ全体のエラーメッセージをまとめて日本語に変えられます。
 *
 * 【たとえ話】
 * 学校の「掲示板」を想像してください。
 *
 * - Step6-05のmessages() = 先生が生徒1人ひとりに個別で手紙を書く
 *   → 正確だけど、生徒が増えると大変
 *
 * - 言語ファイル = 掲示板に「全校生徒へ」と張り紙をする
 *   → 一度書けばみんなに伝わる
 *
 * 【今日学ぶこと】
 * 1. 言語ファイルとは何か
 * 2. 日本語言語ファイルの準備方法
 * 3. フィールド名（attribute）の日本語化
 * 4. messages()との使い分け
 */

/**
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * ✅ 【1. 言語ファイルとは？】
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 *
 * 言語ファイル（Language Files）（ランゲージ ファイルズ）
 * = Laravelのメッセージ・テキストをまとめて管理するファイル
 *
 * 【たとえ話】
 * 辞書を想像してください。
 * 英語→日本語の辞書があれば、英単語をすべて日本語に変換できます。
 * 言語ファイルはその「辞書」の役割を果たします。
 *
 * 「required」という英語のルール名を辞書で調べると
 * 「〜は必須です」という日本語メッセージが見つかる、という仕組みです。
 *
 * 【言語ファイルの場所】
 *
 * Laravel 10では言語ファイルを自分で準備する必要があります。
 * （Laravel 9以前は最初から入っていましたが、10からは別途インストールが必要です）
 *
 * 準備後のファイル場所：
 * laravel-practice/src/laravel/lang/
 *   ├── en/           ← 英語（最初から入っている）
 *   │   ├── validation.php   ← バリデーションメッセージ（英語）
 *   │   ├── auth.php
 *   │   └── ...
 *   └── ja/           ← 日本語（今日追加する）
 *       ├── validation.php   ← バリデーションメッセージ（日本語）
 *       └── ...
 *
 * 【エラーメッセージが作られる流れ】
 * 1. バリデーションが失敗する（例：nameが空欄）
 * 2. Laravelが言語ファイルを開く
 * 3. 「required」のメッセージを探す
 * 4. そのメッセージをユーザーに表示する
 */

/**
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * ✅ 【2. 日本語言語ファイルの準備（Artisanコマンド）】
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 *
 * 【手順1】Laravelの「langフォルダ」を作成する
 *
 * Laravel 10では最初からlangフォルダが存在しない場合があります。
 * 以下のコマンドでlangフォルダと英語ファイルを作成します。
 *
 * ※ Dockerコンテナの中で実行してください
 */

// ターミナルで以下のコマンドをコピーして実行してください：
// php artisan lang:publish

/**
 * → lang/en/ フォルダと英語の言語ファイルが作成されます
 *
 * 【手順2】日本語言語ファイルをインストールする
 *
 * 日本語ファイルは有志の方が作成してくれています。
 * Composer（コンポーザー）というツールを使って追加します。
 *
 * Composer（コンポーザー）= PHPのパッケージ（部品）を管理するツール
 * npm（エヌピーエム）がJavaScriptのパッケージ管理ツールなのと同じ役割です。
 */

// ターミナルで以下のコマンドをコピーして実行してください：
// composer require laravel-lang/lang --dev

/**
 * → 日本語を含む各国語の言語ファイルが追加されます
 *
 * 【手順3】日本語ファイルをlangフォルダにコピーする
 */

// ターミナルで以下のコマンドをコピーして実行してください（1行ずつ実行）：
// php artisan vendor:publish --provider="LaravelLang\Lang\Providers\PackageProvider"

/**
 * → lang/ja/ フォルダが作成されます
 *
 * 【手順4】アプリの言語設定を日本語に変える
 *
 * config/app.php（コンフィグ アップ ピーエイチピー）ファイルの
 * 'locale'（ローカル）という設定を変更します。
 *
 * locale（ローカル）= 「地域の言語設定」という意味
 * 'en' → 'ja' に変えるだけで、アプリ全体が日本語になります。
 */

/**
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * ✅ 【3. config/app.php の設定変更】
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 *
 * ファイルの場所：
 * laravel-practice/src/laravel/config/app.php
 *
 * 【変更する箇所】
 *
 * 変更前：
 * 'locale' => 'en',
 *
 * 変更後：
 * 'locale' => 'ja',
 *
 * 【この設定が何をしているか】
 * Laravelに「日本語の辞書（lang/ja/）を使ってください」と指示しています。
 * この1行を変えるだけで、Laravelが全バリデーションメッセージを
 * lang/ja/validation.phpから読み込むようになります。
 *
 * 【fallback_locale（フォールバック ローカル）について】
 * config/app.phpには 'fallback_locale' => 'en' という設定もあります。
 * これは「日本語ファイルにメッセージが見つからなかった場合、英語で表示する」
 * という予備設定です。基本的にそのままにしておいて問題ありません。
 */

/**
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * ✅ 【4. フィールド名（attribute）の日本語化】
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 *
 * 言語ファイルを設定するとメッセージは日本語になりますが、
 * 「フィールド名」の部分はまだ英語のままです。
 *
 * 【例】
 * 設定後のメッセージ例：
 * 「categoryは必須です。」← まだ英語のフィールド名
 *
 * 理想のメッセージ：
 * 「カテゴリーは必須です。」← 日本語のフィールド名
 *
 * 【フィールド名を日本語にする2つの方法】
 *
 * ───────────────────────────────
 * 方法1：lang/ja/validation.php に書く（アプリ全体に適用）
 * ───────────────────────────────
 *
 * lang/ja/validation.php の一番下に 'attributes' という配列があります。
 * そこにフィールド名の日本語訳を書きます。
 *
 * 【lang/ja/validation.php の attributes 部分（例）】
 *
 * 'attributes' => [
 *     'category'   => 'カテゴリー',
 *     'amount'     => '金額',
 *     'memo'       => 'メモ',
 *     'date'       => '日付',
 *     'name'       => '名前',
 *     'email'      => 'メールアドレス',
 *     'password'   => 'パスワード',
 * ],
 *
 * この書き方のメリット：
 * - 一度書けばアプリ全体のすべてのバリデーションに適用される
 * - FormRequestが複数あっても共通して使える
 *
 * ───────────────────────────────
 * 方法2：FormRequest の attributes() を使う（FormRequest単位で適用）
 * ───────────────────────────────
 *
 * FormRequest（フォームリクエスト）の中に
 * attributes()（アトリビューツ）メソッドを追加します。
 *
 * 【コード例】
 *
 * public function attributes()
 * {
 *     return [
 *         'category' => 'カテゴリー',
 *         'amount'   => '金額',
 *         'memo'     => 'メモ',
 *     ];
 * }
 *
 * この書き方のメリット：
 * - このFormRequestを使うときだけ適用される
 * - 画面ごとに違う表現を使いたい場合に便利
 * （例：「ユーザー登録」画面では「お名前」、「設定」画面では「名前」など）
 *
 * 【どちらを使うべき？】
 * 基本はlang/ja/validation.phpの'attributes'に書く方法（方法1）が楽です。
 * 特別な事情がある場合だけattributes()（方法2）を使います。
 */

/**
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * ✅ 【5. messages() との使い分け】
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 *
 * 【おさらい：messages() とは？】
 * Step6-05で学んだFormRequestのメソッドです。
 * 特定のフィールド×ルールに対して、個別のメッセージを設定できます。
 *
 * 【messages() を使う場面】
 * 言語ファイルの「汎用メッセージ」では伝わりにくい場面に使います。
 *
 * 【たとえ話】
 * 言語ファイル = 学校の一般的なルール（「遅刻しないように」など）
 * messages() = 先生から特定の生徒への個別指導（「あなたは計算ミスが多い」など）
 *
 * 【使い分けの基準】
 *
 * 言語ファイルが向いているケース（汎用メッセージ）：
 * ✅ 「〇〇は必須です。」「〇〇は〜文字以内で入力してください。」
 *    → フィールド名が違うだけで同じパターンのメッセージ
 *
 * messages() が向いているケース（個別メッセージ）：
 * ✅ 「金額は1円以上で入力してください（マイナスは入力できません）」
 *    → そのフィールド固有の詳細な説明が必要
 * ✅ 「メールアドレスは既に登録されています。ログインしてください。」
 *    → ユーザーに次のアクションを案内したい
 * ✅ 「パスワードは英大文字・数字を含む8文字以上で設定してください。」
 *    → フォーマットのルールを具体的に説明したい
 *
 * 【実務での一般的なアプローチ】
 * 1. まず言語ファイルで全体を日本語化する
 * 2. 「このメッセージではユーザーが分かりにくい」と感じた箇所だけ
 *    messages()で個別に上書きする
 */

/**
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * ✅ 【6. 実践：KOHさんのアプリに適用する】
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 *
 * 現在のKOHさんのExpenseController（またはStoreExpenseRequest）は
 * Step6-05でmessages()を使って日本語化してあります。
 *
 * 今日の学習では以下の手順で言語ファイルを使った日本語化に移行します。
 *
 * 【手順1】言語ファイルをインストールする（上記2の手順）
 *
 * 【手順2】config/app.php の locale を 'ja' に変更する
 *
 * 【手順3】lang/ja/validation.php の attributes に追記する
 *
 * 【手順4】StoreExpenseRequest の messages() の中身を確認・整理する
 * - 言語ファイルで対応できるものは削除してOK
 * - 個別説明が必要なものだけ残す
 *
 * 【手順5】動作確認する
 * - 各フィールドを空欄にして送信してみる
 * - エラーメッセージが日本語になっているか確認する
 * - フィールド名（「category」→「カテゴリー」など）も日本語になっているか確認する
 */

/**
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 📚 【用語集】
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 *
 * 言語ファイル（Language Files）（ランゲージ ファイルズ）
 * └─ アプリのメッセージ・テキストをまとめて管理するファイル
 *
 * locale（ローカル）
 * └─ 「地域の言語設定」。'ja'で日本語、'en'で英語になる
 *
 * fallback_locale（フォールバック ローカル）
 * └─ 設定した言語にメッセージがない場合の予備言語
 *
 * Composer（コンポーザー）
 * └─ PHPのパッケージ（部品）を管理するツール。npmのPHP版
 *
 * attributes（アトリビューツ）
 * └─ 「属性」の意味。バリデーションではフィールド名のこと
 *
 * validation.php（バリデーション ピーエイチピー）
 * └─ バリデーションのエラーメッセージをまとめた言語ファイル
 *
 * messages()（メッセージズ）
 * └─ FormRequest内で個別のエラーメッセージを設定するメソッド
 *
 * attributes()（アトリビューツ）
 * └─ FormRequest内でフィールド名の日本語訳を設定するメソッド
 */

/**
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * ❓ 【Q&A】
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 *
 * Q1. 言語ファイルとmessages()、どちらが優先されますか？
 * A1. messages()が優先されます。
 *     言語ファイルは「全体のデフォルト設定」で、
 *     messages()は「そのFormRequestだけの個別設定」です。
 *     個別設定の方が常に優先されます。
 *
 *     たとえ話：
 *     学校の給食（言語ファイル）＝全員に同じメニュー
 *     アレルギー対応食（messages()）＝その子だけの特別メニュー
 *     アレルギー対応食がある場合、その子には特別メニューが出ます。
 *
 * Q2. lang/ja/ フォルダの中のどのファイルがバリデーションに関係しますか？
 * A2. validation.php（バリデーション ピーエイチピー）だけです。
 *     他に auth.php（認証メッセージ）、passwords.php（パスワードメッセージ）
 *     などもありますが、バリデーションには validation.php が対応しています。
 *
 * Q3. 英語のエラーメッセージが出てしまいます。なぜですか？
 * A3. 以下を順番に確認してください：
 *     ① config/app.php の 'locale' が 'ja' になっているか
 *     ② lang/ja/ フォルダが正しく作成されているか
 *     ③ Dockerを再起動したか（設定変更後は再起動が必要な場合があります）
 *
 * Q4. validation.php の attributes に書いたのにフィールド名が英語のままです。
 * A4. ファイルを保存し忘れている可能性が高いです。
 *     もしくは、FormRequest に attributes() メソッドが定義されていて、
 *     そちらの設定が優先されている場合もあります。
 *     両方確認してみてください。
 *
 * Q5. locale を 'ja' にしたら、バリデーション以外にも影響しますか？
 * A5. はい。Laravelが使う言語全体に影響します。
 *     ただしバリデーションメッセージ以外では、
 *     自分でlang/ja/の各ファイルにメッセージを書いた部分だけ日本語になります。
 *     書いていない部分は英語のままです（fallback_localeが'en'のため）。
 */
