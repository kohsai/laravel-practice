/**
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 🧠 【今日学ぶこと】
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 *
 * Step6-05でFormRequestを使ってバリデーションを切り出しました。
 * そのとき、messages()（メッセージズ）を使って個別に日本語のエラーメッセージを設定しました。
 *
 * 【Step6-05のおさらい】
 * public function messages()
 * {
 *     return [
 *         'category.required' => 'カテゴリーを入力してください。',
 *         'amount.required'   => '金額を入力してください。',
 *     ];
 * }
 *
 * この書き方には1つ問題があります。
 * 「フィールドごとに1つ1つ日本語メッセージを書く必要がある」ということです。
 * フィールドが10個、20個と増えると、messages()がどんどん長くなってしまいます。
 *
 * 【今日学ぶ解決策】
 * Laravelには「言語ファイル（げんごふぁいる）」という仕組みがあります。
 * アプリ全体のエラーメッセージをまとめて日本語に変えられます。
 *
 * 【たとえ話】
 * 学校の「掲示板」を想像してください。
 *
 * - Step6-05のmessages() = 先生が生徒1人ひとりに個別で手紙を書く
 *   → 正確だけど、生徒が増えると大変
 *
 * - 言語ファイル = 掲示板に「全校生徒へ」と張り紙をする
 *   → 一度書けばみんなに伝わる
 *
 * 【今日学ぶこと】
 * 1. 言語ファイルとは何か
 * 2. 日本語言語ファイルの準備方法
 * 3. フィールド名（attribute）の日本語化
 * 4. messages()との使い分け
 */

/**
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * ✅ 【1. 言語ファイルとは？】
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 *
 * 言語ファイル（Language Files）（ランゲージ ファイルズ）
 * = Laravelのメッセージ・テキストをまとめて管理するファイル
 *
 * 【たとえ話】
 * 辞書を想像してください。
 * 英語→日本語の辞書があれば、英単語をすべて日本語に変換できます。
 * 言語ファイルはその「辞書」の役割を果たします。
 *
 * 「required」という英語のルール名を辞書で調べると
 * 「〜は必須です」という日本語メッセージが見つかる、という仕組みです。
 *
 * 【Laravel 9以前と10以降の違い】
 *
 * Laravel 9以前：
 * - 言語ファイルが最初から resources/lang/ に入っていた
 * - インストール直後からすぐ使える状態だった
 *
 * Laravel 10以降（10・11・12・13）：
 * - 言語ファイルがデフォルトでは入っていない
 * - php artisan lang:publish で自分で作成する必要がある
 * - 正式な場所は lang/（プロジェクト直下）に変更された
 * - ただし後方互換性のため resources/lang/ も読みに行く
 * - しかも resources/lang/ の方が優先される
 *
 * 【KOHさんの環境（Laravel 10）の実際のファイル場所】
 * resources/lang/
 *   └── ja/
 *       ├── auth.php
 *       ├── pagination.php
 *       ├── passwords.php
 *       └── validation.php  ← attributesに日本語フィールド名を追記済み
 *
 * 【エラーメッセージが作られる流れ】
 * 1. バリデーションが失敗する（例：categoryが空欄）
 * 2. Laravelが resources/lang/ja/ を開く
 * 3. 「required」のメッセージを探す
 * 4. attributesで「category」→「カテゴリ」に変換する
 * 5. 「カテゴリは必須項目です。」とユーザーに表示する
 */

/**
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * ✅ 【2. 日本語言語ファイルの準備（実際に行った手順）】
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 *
 * 【実際に行った手順（KOHさんの環境での記録）】
 *
 * 手順1：lang:publish でlangフォルダを作成
 * → lang/en/ フォルダと英語ファイルが作成された
 *
 * 手順2：composer require laravel-lang/lang --dev で日本語パッケージをインストール
 * → laravel-lang/lang 14.8.0 がインストールされた
 *   （最新のv15はPHP 8.2以上必須のため、PHP 8.1のKOHさんの環境ではv14が自動選択された）
 *
 * 手順3：php artisan lang:add ja で日本語ファイルを追加
 * → resources/lang/ja/ に日本語ファイルが作成された
 *   （Laravel 10の後方互換性により resources/lang/ に作られた）
 *
 * 手順4：config/app.php の locale を 'ja' に変更
 * → すでに 'ja' になっていた（事前に設定済みだった）
 *
 * 手順5：resources/lang/ja/validation.php の attributes にフィールド名を追記
 * → category・amount・spent_at を日本語で追記した
 *
 * 【PHPバージョンとパッケージバージョンの関係】
 *
 * KOHさんの環境のPHPバージョンは 8.1 です。
 * これは docker/php/Dockerfile の1行目で指定されています：
 * FROM php:8.1-fpm
 *
 * laravel-lang/lang のバージョン対応：
 * - v14：PHP 8.1でも動く（KOHさんの環境で使用）
 * - v15（最新）：PHP 8.2以上が必要
 *
 * GodeVen（Laravel 12/13）を作るときは、Dockerfileの8.1を8.2に変更すれば
 * PHP 8.2環境になり、v15（最新）がそのまま使えるようになります。
 */

/**
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * ✅ 【3. config/app.php の設定変更】
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 *
 * ファイルの場所：
 * laravel-practice/src/laravel/config/app.php
 *
 * 【変更する箇所】
 *
 * 変更前：
 * 'locale' => 'en',
 *
 * 変更後：
 * 'locale' => 'ja',
 *
 * 【この設定が何をしているか】
 * Laravelに「日本語の辞書を使ってください」と指示しています。
 * この1行を変えるだけで、Laravelが全バリデーションメッセージを
 * 日本語ファイルから読み込むようになります。
 *
 * 【fallback_locale（フォールバック ローカル）について】
 * config/app.phpには 'fallback_locale' => 'ja' という設定もあります。
 * これは「日本語ファイルにメッセージが見つからなかった場合、日本語で表示する」
 * という予備設定です。KOHさんの環境では 'ja' に設定済みです。
 */

/**
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * ✅ 【4. フィールド名（attribute）の日本語化】
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 *
 * 言語ファイルを設定するとメッセージは日本語になりますが、
 * 「フィールド名」の部分はまだ英語のままです。
 *
 * 【例】
 * 設定直後のメッセージ：
 * 「Categoryは必須項目です。」← まだ英語のフィールド名
 *
 * attributes追記後のメッセージ：
 * 「カテゴリは必須項目です。」← 日本語のフィールド名
 *
 * 【KOHさんの環境で追記した内容】
 * resources/lang/ja/validation.php の attributes：
 *
 * 'attributes' => [
 *     'description'           => '詳細',
 *     'email'                 => 'メールアドレス',
 *     'name'                  => 'ユーザー名',
 *     'password'              => 'パスワード',
 *     'password_confirmation' => 'パスワード（確認）',
 *     'title'                 => 'タイトル',
 *     'category'              => 'カテゴリ',   ← 追記
 *     'amount'                => '金額',       ← 追記
 *     'spent_at'              => '日付',       ← 追記
 * ],
 *
 * 【フィールド名を日本語にする2つの方法】
 *
 * 方法1：resources/lang/ja/validation.php に書く（アプリ全体に適用）
 * - 一度書けばアプリ全体のすべてのバリデーションに適用される
 * - FormRequestが複数あっても共通して使える
 * - 基本はこちらを使う
 *
 * 方法2：FormRequest の attributes() メソッドを使う（FormRequest単位で適用）
 * - このFormRequestを使うときだけ適用される
 * - 画面ごとに違う表現を使いたい場合に便利
 * - 特別な事情がある場合だけ使う
 */

/**
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * ✅ 【5. messages() との使い分け】
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 *
 * 【messages() とは？】
 * Step6-05で学んだFormRequestのメソッドです。
 * 特定のフィールド×ルールに対して、個別のメッセージを設定できます。
 *
 * 【優先順位】
 * messages() > 言語ファイル
 * messages()が設定されている場合、言語ファイルより優先されます。
 *
 * たとえ話：
 * 学校の給食（言語ファイル）＝全員に同じメニュー
 * アレルギー対応食（messages()）＝その子だけの特別メニュー
 * アレルギー対応食がある場合、その子には特別メニューが出ます。
 *
 * 【使い分けの基準】
 *
 * 言語ファイルが向いているケース：
 * ✅ 「〇〇は必須です。」「〇〇は〜文字以内で入力してください。」
 *    → フィールド名が違うだけで同じパターンのメッセージ
 *
 * messages() が向いているケース：
 * ✅ 「金額は1円以上で入力してください（マイナスは入力できません）」
 *    → そのフィールド固有の詳細な説明が必要
 * ✅ 「メールアドレスは既に登録されています。ログインしてください。」
 *    → ユーザーに次のアクションを案内したい
 * ✅ 「カテゴリは食費・交通費・娯楽費・その他から選んでください」
 *    → 選べる値を具体的に案内したい
 *
 * 【実務での一般的なアプローチ】
 * 1. まず言語ファイルで全体を日本語化する
 * 2. 「このメッセージではユーザーが分かりにくい」と感じた箇所だけ
 *    messages()で個別に上書きする
 *
 * 【KOHさんのアプリの現在の状態】
 * messages() に以下が設定されており、言語ファイルより優先されています：
 * - category.required → 「カテゴリは必ず選択してください」
 * - category.in       → 「カテゴリは食費・交通費・娯楽費・その他から選んでください」
 * - amount.required   → 「金額は必ず入力してください」
 * - amount.integer    → 「金額は数字で入力してください」
 * - amount.min        → 「金額は1円以上で入力してください」
 * - amount.max        → 「金額は9,999,999円以下で入力してください」
 * - description.max   → 「説明は200文字以内で入力してください」
 * - spent_at.required → 「日付は必ず入力してください」
 * - spent_at.date     → 「日付の形式が正しくありません」
 * - spent_at.before_or_equal → 「日付は今日以前の日付を入力してください」
 */

/**
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * ✅ 【6. GodeVen（Laravel 12/13）での日本語化手順】
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 *
 * 基本的な手順はLaravel 10と同じです。
 * 違いはパッケージのバージョン指定が不要になる点だけです。
 *
 * 手順1：composer require laravel-lang/lang --dev
 * → Laravel 12/13 + PHP 8.2以上の環境では最新のv15がそのまま入る
 * → バージョン指定不要
 *
 * 手順2：php artisan lang:publish
 * → lang/ フォルダと英語ファイルが作成される
 *
 * 手順3：php artisan lang:add ja
 * → 日本語ファイルが追加される
 * → どのフォルダに作られるかを ls コマンドで確認すること（重要）
 *
 * 手順4：config/app.php の locale を 'ja' に変更
 *
 * 手順5：validation.php の attributes にフィールド名を追記
 *
 * 【注意点】
 * 手順3で日本語ファイルがどのフォルダに作られるかを必ず確認する。
 * lang/ja/ に作られるか resources/lang/ja/ に作られるかによって
 * 編集するファイルの場所が変わります。
 * 今回のように「間違った場所を編集する」ミスを防ぐためです。
 */

/**
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 📚 【用語集】
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 *
 * 言語ファイル（Language Files）（ランゲージ ファイルズ）
 * └─ アプリのメッセージ・テキストをまとめて管理するファイル
 *
 * locale（ローカル）
 * └─ 「地域の言語設定」。'ja'で日本語、'en'で英語になる
 *
 * fallback_locale（フォールバック ローカル）
 * └─ 設定した言語にメッセージがない場合の予備言語
 *
 * Composer（コンポーザー）
 * └─ PHPのパッケージ（部品）を管理するツール。npmのPHP版
 *
 * attributes（アトリビューツ）
 * └─ 「属性」の意味。バリデーションではフィールド名のこと
 *
 * validation.php（バリデーション ピーエイチピー）
 * └─ バリデーションのエラーメッセージをまとめた言語ファイル
 *
 * messages()（メッセージズ）
 * └─ FormRequest内で個別のエラーメッセージを設定するメソッド
 *
 * attributes()（アトリビューツ）
 * └─ FormRequest内でフィールド名の日本語訳を設定するメソッド
 *
 * Dockerfile（ドッカーファイル）
 * └─ Dockerの環境設定ファイル。PHPのバージョンなどを指定する
 *    KOHさんの環境：docker/php/Dockerfile の FROM php:8.1-fpm
 */

/**
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * ❓ 【Q&A（学習中に出た質問）】
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 *
 * Q1. 言語ファイルとmessages()、どちらが優先されますか？
 * A1. messages()が優先されます。
 *     言語ファイルは「全体のデフォルト設定」で、
 *     messages()は「そのFormRequestだけの個別設定」です。
 *     個別設定の方が常に優先されます。
 *
 * Q2. lang/ja/ と resources/lang/ja/ どちらが正しい場所ですか？
 * A2. Laravel 10では両方読みに行きますが、resources/lang/ が優先されます。
 *     今回のKOHさんの環境では resources/lang/ja/ が正しい場所です。
 *     Laravel 9以前は resources/lang/ が唯一の場所でした。
 *     Laravel 10から lang/（プロジェクト直下）が新しい正式な場所になりましたが、
 *     後方互換性のため resources/lang/ も引き続き動きます。
 *
 * Q3. PHPのバージョン（8.1や8.2）はどこで決まりますか？
 * A3. Dockerの設定ファイル（docker/php/Dockerfile）の1行目で決まります。
 *     FROM php:8.1-fpm の「8.1」の部分です。
 *     GodeVenでLaravel 12/13を使う場合は「8.2」以上に変更する必要があります。
 *
 * Q4. laravel-lang/lang のv14とv15の違いは何ですか？
 * A4. 動作に大きな違いはありません。
 *     v15はPHP 8.2以上が必要なだけで、日本語ファイルの内容は同じです。
 *     KOHさんの環境はPHP 8.1のためv14が自動的に選ばれました。
 *     GodeVen（PHP 8.2以上）ではv15がそのまま使えます。
 *
 * Q5. 「validation.required」という文字がそのまま表示されました。なぜですか？
 * A5. 言語ファイルが正しく読み込まれていない状態です。
 *     今回は resources/lang/ja/ を誤って削除したことが原因でした。
 *     対処法：
 *     ① config/app.php の locale が 'ja' になっているか確認
 *     ② 日本語ファイルが正しい場所にあるか確認（ls コマンドで確認）
 *     ③ php artisan config:clear でキャッシュをクリア
 *
 * Q6. 日本語化の手順はLaravelのバージョンで変わりますか？
 * A6. 手順自体は変わりません。
 *     変わるのはlaravel-lang/langのバージョン指定だけです。
 *     - Laravel 10（PHP 8.1）：v14を手動指定が必要な場合あり
 *     - Laravel 11〜13（PHP 8.2以上）：v15（最新）がそのまま使える
 */
