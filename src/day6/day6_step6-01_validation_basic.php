 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 💬 【第2部：学習中のQ&A】
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 */

/**
 * Q1. `public function store(Request $request)` の各部分の意味は？
 *
 * ■ public（パブリック）
 * 「外から呼び出せる」という意味。フォームからアクセスできるようにするために必要。
 * 3種類ある：
 * - public    = どこからでも呼び出せる（店の入口：誰でも入れる）
 * - protected = 同じクラスと子クラスだけ（従業員通路：スタッフのみ）
 * - private   = 同じクラスの中だけ（金庫室：オーナーのみ）
 * コントローラーでフォームから呼ばれるメソッドは必ず public。
 *
 * ■ function（ファンクション）
 * 「この名前でこういう処理を作ります」という宣言。
 * 「定義する」＝「名前をつけて処理を作る」と覚えておけば十分。
 *
 * ■ Request $request（リクエスト、ダラーリクエスト）
 * - Request（大文字・型名） = 「フォームから届くデータの種類」を表す設計図
 *                             「届くデータはRequestという種類ですよ」とLaravelに教える
 * - $request（小文字・変数）= 実際に届いたデータそのものが入る「箱」
 *
 * たとえ話：
 * - Request  = 「宅急便」という種類の名前
 * - $request = 実際に届いた宅急便の箱
 *
 * ■ まとめると
 * 「公開形式（public）だから外から呼び出せる。
 *  定義（function）するのは今回は『保存（store）』。
 *  フォームからRequestという種類のデータが来るから、$requestに入れる。」
 */

/**
 * Q2. `$request->validate([...])` の意味は？
 *
 * - $request   = 届いた荷物（フォームのデータ）
 * - ->         = 「の中を開いて」
 * - validate() = 「中身をチェックする」
 *
 * 「届いた荷物を開けて、中身（category・amountなど）が正しいかチェックして」
 * という命令です。
 */

/**
 * Q3. `Expense::create($request->all())` と `return redirect('/expenses')` の意味は？
 *
 * ■ Expense::create($request->all())
 * - Expense::create(...) = 「Expenseテーブルに新しいデータを1件作成して」
 * - $request->all()      = 「フォームから届いたデータを全部」
 * → 「フォームのデータ全部を使って、Expenseテーブルに保存して」
 *
 * ■ return redirect('/expenses')
 * - return     = 「この処理はここで終わり」という宣言
 * - redirect() = 「別のページに移動させる」
 * → 「保存が終わったら、処理を終えて /expenses のページに移動して」
 */

/**
 * Q4. `$request->all()` の all() 以外のパターンは？
 *
 * all() は「フォームのデータを全部渡す」ですが、他にも書き方があります。
 *
 * $request->only(['category', 'amount'])  → categoryとamountだけ渡す
 * $request->except(['_token'])            → _token以外を全部渡す
 * $request->input('category')             → categoryの値だけ1つ取り出す
 *
 * 実務では all() より only() の方が安全とされています。
 * 理由：「想定外の項目まで保存してしまうリスク」を防げるから。
 * ただし今は all() で問題ありません。
 */

