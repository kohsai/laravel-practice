/**
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 🧠 【今日学ぶこと】
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 *
 * バリデーション（Validation）（バリデーション） = 「入力チェック」のこと
 *
 * ユーザーがフォームに入力したデータが「正しいかどうか」を確認する仕組みです。
 *
 * 【なぜ必要なの？】
 *
 * たとえば、こんな困ったことが起きる可能性があります：
 * - 名前を入力してほしいのに、空欄のまま送信された
 * - メールアドレスを入力してほしいのに「あいうえお」と入力された
 * - 金額を入力してほしいのに、文字が入力された
 *
 * バリデーションを使えば、こういった「おかしな入力」を自動的に弾いてくれます。
 *
 * 【たとえ話】
 * バリデーション = 入口にいる「門番」
 *
 * お城（データベース）への入口に門番が立っていて、
 * 「名前を言ってください」「パスポートを見せてください」などの
 * チェックをして、条件を満たした人だけ中に入れてくれます。
 * 条件を満たさない人には「やり直してください」と伝えます。
 *
 * 【今日学ぶ内容】
 * 1. バリデーションの基本的な書き方
 * 2. よく使うバリデーションのルール
 * 3. バリデーションが失敗したときの動き
 */

/**
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * ✅ 【1. バリデーションの基本的な書き方】
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 *
 * バリデーションは「コントローラー（Controller）」（コントローラー）の中に書きます。
 *
 * コントローラー = 「司令塔」。ユーザーの操作を受け取って、
 *                  データベースやビューに指示を出す役割
 *
 * 【基本の書き方】
 *
 * $request->validate([
 *     '項目名' => 'ルール',
 * ]);
 *
 * $request（リクエスト） = ユーザーが送ってきたデータ全体
 * validate（バリデート） = 「検証する」という意味
 * []（カクカッコ）       = チェックする項目とルールの一覧
 *
 * 【実際のコード例】
 *
 * 「カテゴリ・金額・説明・日付」をチェックする場合：
 *
 * public function store(Request $request)
 * {
 *     // ↓ここがバリデーション
 *     $request->validate([
 *         'category'    => 'required|string|max:50',
 *         'amount'      => 'required|integer|min:1',
 *         'description' => 'nullable|string|max:255',
 *         'spent_at'    => 'required|date',
 *     ]);
 *
 *     // バリデーションを通過した場合だけ、ここから下が実行される
 *     Expense::create($request->all());
 *     return redirect('/expenses');
 * }
 *
 * 【ポイント】
 * - validate() が成功すれば → その下のコードが実行される
 * - validate() が失敗すれば → 自動的に前のページに戻り、エラーが表示される
 *
 * つまり「門番が止めてくれたら、自動的にやり直しを促してくれる」仕組みです！
 *
 * ※ ExpenseController.php はまだ存在しません。Step6-02で実際に作成します。
 *   ここでは「こういうコードを書く」というイメージを掴んでください。
 */

/**
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * ✅ 【2. よく使うバリデーションのルール一覧】
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 *
 * ルールは「|」（パイプ）でつなげて複数指定できます。
 * 「|」（パイプ） = 「かつ」という意味。複数のルールを組み合わせる記号
 *
 * 例：'required|string|max:50'
 * → 「必須 かつ 文字列 かつ 50文字以内」という意味
 */

/**
 * 【必須・任意のルール】
 *
 * required（リクワイアード） = 「必須」。空欄は許可しない
 * nullable（ヌラブル）       = 「空欄OK」。入力しなくてもよい
 *
 * たとえ話：
 * required = 履歴書の「名前欄」（必ず書く）
 * nullable = 履歴書の「趣味欄」（書いても書かなくてもよい）
 */

/**
 * 【データの種類のルール】
 *
 * string（ストリング）     = 文字列（テキスト）であること
 * integer（インテジャー）  = 整数（1, 2, 100 など）であること
 * numeric（ニューメリック）= 数値（1.5 など小数もOK）であること
 * boolean（ブーリアン）    = true か false であること
 * date（デイト）           = 日付形式（2026-01-01 など）であること
 * email（イーメール）      = メールアドレス形式であること
 */

/**
 * 【文字数・数値の範囲のルール】
 *
 * max:数値（マックス）    = 最大〇〇まで
 * min:数値（ミン）        = 最小〇〇以上
 * between:最小,最大       = 〇〇から〇〇の間
 */

/**
 * 【その他よく使うルール】
 *
 * unique:テーブル名（ユニーク）  = データベースに同じ値がないこと
 * exists:テーブル名（イグジスツ）= データベースに存在する値であること
 * in:値1,値2（イン）             = 指定した値の中に含まれていること
 * confirmed（コンファームド）    = 確認用の項目と一致すること（パスワード確認など）
 *
 * 例：
 * 'email'    => 'unique:users'           // usersテーブルに同じメールがない
 * 'category' => 'in:食費,交通費,娯楽費' // この3つのどれか
 * 'password' => 'confirmed'              // password_confirmationと一致
 */

/**
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * ✅ 【3. バリデーションが失敗したときの動き】
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 *
 * バリデーションが失敗すると、Laravelが自動的に以下のことをしてくれます：
 *
 * 1. 前のページ（フォームのページ）に自動的に戻る
 * 2. エラーメッセージを「$errors」（エラーズ）という変数に入れてくれる
 * 3. 入力していた内容（古い入力値）を「old()」（オールド）で取り出せるようにしてくれる
 *
 * 【たとえ話】
 * 門番にはじかれたお客さんが、
 * 「どこが間違っていたか」のメモをもらって入口に戻ってくる感じです。
 *
 * 【Bladeで$errorsを表示する方法】
 * ※ Blade構文の詳細はDay9で学びます。今は「$errorsにエラーが入る」とだけ理解すればOK
 *
 * @if ($errors->any())
 *     <ul>
 *         @foreach ($errors->all() as $error)
 *             <li>{{ $error }}</li>
 *         @endforeach
 *     </ul>
 * @endif
 */

/**
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 📚 【用語集】
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 *
 * バリデーション（Validation）
 * └─ 入力データが正しいかどうかをチェックする仕組み。「入力検証」とも言う
 *
 * validate()（バリデート）
 * └─ バリデーションを実行するメソッド。$request->validate([ルール一覧]) の形で使う
 *
 * $request（リクエスト）
 * └─ ユーザーがフォームから送ってきたデータ全体が入っている変数
 *
 * $errors（エラーズ）
 * └─ バリデーション失敗時に、エラーメッセージが自動的に入る変数
 *
 * required（リクワイアード）
 * └─ 「必須」のルール。空欄は許可しない
 *
 * nullable（ヌラブル）
 * └─ 「空欄OK」のルール。入力しなくてもよい
 *
 * | （パイプ）
 * └─ ルールを複数つなげる記号。「かつ」という意味
 */

