/**
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * ✅ 【6. よくある質問（Q&A）】
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 *
 * Q1. $errors はどこから来るのですか？自分で用意するのですか？
 * A1. Laravelが自動で用意してくれます。自分で書く必要はありません。
 *     バリデーションが失敗すると、Laravelが「$errors」に
 *     エラーメッセージを入れてBladeに渡してくれます。
 *
 * Q2. @error と @if ($errors->has('項目名')) の違いは何ですか？
 * A2. 結果は同じです。@error の方が短く書けます。
 *     @error('category') は @if ($errors->has('category')) の省略形です。
 *     実務では短い @error を使うのが一般的です。
 *
 * Q3. $message は自分で定義するのですか？
 * A3. 定義不要です。@error ブロックの中で自動で使えます。
 *     @error('category') と書いた場合、$message には
 *     'category'のエラーメッセージが自動で入ります。
 *
 * Q4. old() はなぜ必要ですか？なくてもいいですか？
 * A4. なくても動きます。ただし、バリデーション失敗後に入力内容が消えます。
 *     old() を使うと消えないので、ユーザーフレンドリーになります。
 *     実務では必ず使います。
 *
 * Q5. name="category" と @error('category') の 'category' は
 *     なぜ同じにしないといけないのですか？
 * A5. Laravelがこの名前を使ってエラーを紐づけているからです。
 *     コントローラーの validate(['category' => ...]) も同じ名前です。
 *     3箇所すべて同じ名前にすることで「この入力欄のエラー」と特定できます。
 *     名前が違うと「どの欄のエラーか」わからなくなります。
 *
 * Q6. エラーメッセージが英語で表示されます。日本語にできますか？
 * A6. できます！これはStep6-06「カスタムエラーメッセージと日本語化」で学びます。
 *
 * Q7. @csrf は何のためにあるのですか？
 * A7. セキュリティのためです。CSRF（シーサーフ）という攻撃を防ぎます。
 *     POSTフォームには必ず書く決まりです。書き忘れると419エラーになります。
 */

/**
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 📚 【用語集】
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 *
 * $errors（エラーズ）
 * └─ バリデーション失敗時にLaravelが自動で用意するエラーの入れ物
 *    すべてのBladeテンプレートで自動で使える特別な変数
 *
 * ->any()（エニー）
 * └─ 「何か1つでもあるか？」を確認する命令
 *    エラーが1件以上あれば true、なければ false
 *
 * ->all()（オール）
 * └─ 「全部取り出す」命令
 *    全エラーメッセージを配列で返す
 *
 * @error（アットエラー）
 * └─ 特定の入力項目にエラーがある場合だけ表示するBladeの命令
 *    @error('項目名') 〜 @enderror の形で使う
 *
 * $message（メッセージ）
 * └─ @error ブロックの中だけで使えるエラーメッセージの変数
 *    自分で定義しなくてよい、Laravelが自動で入れてくれる
 *
 * old()（オールド）
 * └─ バリデーション失敗後に、前回入力した値を取り出す関数
 *    value="{{ old('項目名') }}" の形で input タグに使う
 *
 * @csrf（アットシーサーフ）
 * └─ セキュリティ用のトークンを埋め込む命令
 *    POSTフォームには必ず書く。書き忘れると419エラー
 *
 * {{ }}（ダブルカーリーブレース）
 * └─ 変数の中身をHTMLに表示する記号
 *
 * @if / @endif（アットイフ / アットエンドイフ）
 * └─ 条件分岐。@if(条件) 〜 @endif で囲む
 *
 * @foreach / @endforeach（アットフォーイーチ / アットエンドフォーイーチ）
 * └─ 繰り返し。複数のエラーメッセージを1つずつ表示するときに使う
 */

/**
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 💬 【第2部：学習中に出た質問と回答】
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 *
 * Q1. PHPとBladeの違いは何ですか？@をつける意味は？
 *
 * A1. PHPは「プログラムを書く言語」、Bladeは「HTMLの中にPHPを
 *     埋め込みやすくしたLaravel専用の書き方」です。
 *     @ は「これはBladeの命令ですよ」という印です。
 *
 *     PHPで書く場合：
 *     <?php if ($errors->any()): ?>
 *         表示内容
 *     <?php endif; ?>
 *
 *     Bladeで書く場合：
 *     @if ($errors->any())
 *         表示内容
 *     @endif
 *
 *     結果は全く同じです。Bladeの方が短くHTMLに馴染みやすいので
 *     Laravelではこちらを使います。
 *
 * ──────────────────────────────────────────
 *
 * Q2. @ifと@errorは両方Blade文ですか？違いは何ですか？
 *
 * A2. 両方Blade命令です。
 *
 *     @if ($errors->any()) = 「エラーが存在するか？」という存在確認
 *     → 全件まとめ表示で使うのが典型的な使い方
 *
 *     @error('category') = 「'category'にエラーがあるか？」という特定項目の確認
 *     → 各入力欄の横に使うのが典型的な使い方
 *
 *     ただし @error は @if ($errors->has('category')) の短縮形なので、
 *     本質的には両方とも「条件分岐（if）」です。
 *     @error が特別な仕組みというわけではなく、
 *     よく使うパターンを短く書けるようにしたものです。
 *
 * ──────────────────────────────────────────
 *
 * Q3. 組み立ての順番（1→2→3→4）になる理由は？
 *
 * A3. 後のものは前のものがないと成立しない「依存関係」があるからです。
 *
 *     1→2の理由：
 *     inputタグがないと value="{{ old() }}" を書く場所がない。
 *     まずinputを作ってから、その属性としてold()を追加する。
 *
 *     2→3の理由：
 *     @error('category') はinputの name="category" と対になっている。
 *     inputが存在してから、その直下にエラー表示を追加するのが自然。
 *
 *     3→4の理由：
 *     全件まとめ表示は「全入力欄のエラーをまとめて出す」もの。
 *     各入力欄が出揃ってから、まとめをフォームの上に置く。
 *
 *     たとえ話：「まず部屋に家具を置く（1・2・3）、
 *     それから部屋全体の案内板を玄関に貼る（4）」のイメージ。
 *
 * ──────────────────────────────────────────
 *
 * 【練習問題の解答】
 *
 * 問題1：A = if　B = any　→ ✅ 正解
 * 問題2：C = error　D = amount　E = message　F = enderror　→ ✅ 正解
 *         ※ E は {{ $（E）}} の形なので $ を除いた message が答え
 * 問題3：G = old　H = spent_at　→ ✅ 正解
 */

