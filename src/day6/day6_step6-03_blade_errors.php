/**
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 🧠 【今日学ぶこと】
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 *
 * Step6-02で「バリデーション（入力チェック）」をコントローラーに書きました。
 * でも、チェックに失敗したとき、ユーザーに「何が間違っているか」を
 * 画面で伝えることが大切です。
 *
 * このStepでは、Blade（ブレード）テンプレートの上に
 * エラーメッセージを表示する方法を学びます。
 *
 * 【今日学ぶ3つのこと】
 * 1. $errors（エラーズ）変数 = Laravelが自動で用意するエラーの入れ物
 * 2. @error（アットエラー）= 特定の項目のエラーだけ表示する命令
 * 3. old()（オールド） = 入力した値を保持する仕組み
 *
 * 【Laravel全体での位置づけ】
 * ユーザーが入力 → コントローラーでチェック → エラーがあればBladeで表示
 * このStepはその「Bladeで表示」の部分を担当します。
 */

/**
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * ✅ 【前提の復習：バリデーションが失敗するとどうなる？】
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 *
 * Step6-02で書いたコードを思い出してください。
 *
 * 【ExpenseController の store() メソッド（復習）】
 *
 * public function store(Request $request)
 * {
 *     $request->validate([
 *         'category'    => 'required|string|max:50',
 *         'amount'      => 'required|integer|min:1',
 *         'description' => 'nullable|string|max:255',
 *         'spent_at'    => 'required|date',
 *     ]);
 * }
 *
 * 【バリデーションが失敗したとき、Laravelは自動でこうする】
 * 1. 直前のページ（フォームページ）に戻す
 * 2. エラーメッセージを「$errors（エラーズ）」という変数に入れる
 * 3. 入力した値を「old()（オールド）」という仕組みで保持する
 *
 * 【たとえ話】
 * 学校の提出物チェックに例えると...
 * - validate() = 先生が提出物をチェックする
 * - バリデーション失敗 = 「ここが抜けてるよ、書き直して」と返される
 * - $errors = 先生が書いてくれた「ここが間違い」メモ
 * - old() = 書き直すとき、前に書いた内容がそのまま残っている
 */

/**
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * ✅ 【1. $errors 変数を1行ずつ分解して理解する】
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 *
 * 【まず完成形を見てみましょう】
 *
 * @if ($errors->any())
 *     <ul>
 *         @foreach ($errors->all() as $error)
 *             <li>{{ $error }}</li>
 *         @endforeach
 *     </ul>
 * @endif
 *
 * 「なんだこれ…」と思いますよね。1行ずつ分解します。
 *
 * ──────────────────────────────────────────
 * 【1行目】@if ($errors->any())
 * ──────────────────────────────────────────
 *
 * @if（アットイフ）
 * └─ 「もし〜ならば」という意味のBlade命令
 * └─ PHPの if() と同じ意味。Bladeでは @ をつけて書く
 *
 * $errors（エラーズ）
 * └─ Laravelがバリデーション失敗時に自動で用意する変数
 * └─ 自分で作らなくてよい。Laravelが勝手に準備してくれる
 * └─ 「先生が書いてくれたメモ帳」のイメージ
 *
 * ->any()（アロー エニー）
 * └─ 「メモ帳に何か書いてあるか？」を確認する命令
 * └─ エラーが1つでもあれば true（トゥルー）= 本当
 * └─ エラーが1つもなければ false（フォルス）= 嘘
 *
 * つまり @if ($errors->any()) = 「エラーが1つでもあれば以下を表示する」
 *
 * ──────────────────────────────────────────
 * 【2行目】@foreach ($errors->all() as $error)
 * ──────────────────────────────────────────
 *
 * @foreach（アットフォーイーチ）
 * └─ 「〜を1つずつ取り出して繰り返す」という意味のBlade命令
 * └─ PHPの foreach() と同じ意味
 *
 * $errors->all()（オール）
 * └─ 「メモ帳の内容を全部出して」という命令
 * └─ エラーメッセージが配列（はいれつ）で返ってくる
 * └─ 例：['category は必須項目です。', 'amount は必須項目です。']
 *
 * as $error
 * └─ 「取り出した1件を $error という箱に入れる」という意味
 * └─ 1回目のループ：$error = 'category は必須項目です。'
 * └─ 2回目のループ：$error = 'amount は必須項目です。'
 *
 * ──────────────────────────────────────────
 * 【3行目】<li>{{ $error }}</li>
 * ──────────────────────────────────────────
 *
 * {{ }}（ダブルカーリーブレース）
 * └─ 「この変数の中身をHTMLに表示する」という記号
 * └─ {{ $error }} = $error の中身をそのまま画面に出す
 *
 * <li>（エルアイ）
 * └─ HTMLの「箇条書きの1行」を作るタグ
 * └─ <ul> の中に入れると ・ がついた箇条書きになる
 *
 * ──────────────────────────────────────────
 * 【全体の流れをまとめると】
 * ──────────────────────────────────────────
 *
 * @if ($errors->any())    ← エラーがあれば...
 *     <ul>
 *         @foreach ($errors->all() as $error)  ← 全エラーを1つずつ取り出して...
 *             <li>{{ $error }}</li>             ← 箇条書きで表示する
 *         @endforeach     ← 繰り返し終わり
 *     </ul>
 * @endif                  ← if終わり
 */

/**
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * ✅ 【2. @error を1行ずつ分解して理解する】
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 *
 * 【まず完成形を見てみましょう】
 *
 * <input type="text" name="category" value="{{ old('category') }}">
 * @error('category')
 *     <span style="color: red;">{{ $message }}</span>
 * @enderror
 *
 * 1行ずつ分解します。
 *
 * ──────────────────────────────────────────
 * 【1行目】<input ... value="{{ old('category') }}">
 * ──────────────────────────────────────────
 *
 * <input>（インプット）
 * └─ HTMLの「入力欄」を作るタグ
 *
 * name="category"
 * └─ この入力欄の「名前」
 * └─ コントローラーの validate() で書いた 'category' と一致させる
 * └─ 名前が一致しないとバリデーションが機能しない
 *
 * value="{{ old('category') }}"
 * └─ この入力欄に最初から入れておく値
 * └─ old('category') = 「前回入力した 'category' の値を取り出す」
 * └─ 初回表示時（まだ何も入力していない）= 空っぽ
 * └─ バリデーション失敗後 = 前に入力した値がそのまま入る
 *
 * 【なぜ old() が必要か？】
 * old() なし → 「食費」と入力 → 送信 → エラー → 「食費」が消える → 入力し直し
 * old() あり → 「食費」と入力 → 送信 → エラー → 「食費」がそのまま残る
 * ユーザーにとって後者の方がずっと使いやすい。
 *
 * ──────────────────────────────────────────
 * 【2行目】@error('category')
 * ──────────────────────────────────────────
 *
 * @error（アットエラー）
 * └─ 「特定の項目にエラーがあれば以下を表示する」というBlade命令
 * └─ @if ($errors->has('category')) と同じ意味だが、短く書ける
 *
 * ('category')
 * └─ 「'category' という名前の入力欄のエラーを確認する」
 * └─ name="category" の入力欄に対応している
 *
 * ──────────────────────────────────────────
 * 【3行目】<span style="color: red;">{{ $message }}</span>
 * ──────────────────────────────────────────
 *
 * $message（メッセージ）
 * └─ @error ブロックの中だけで使える特別な変数
 * └─ 自分で定義しなくてよい。Laravelが自動で入れてくれる
 * └─ 中身 = 'category は必須項目です。' のようなエラーメッセージ
 *
 * <span style="color: red;">
 * └─ 「この中の文字を赤色にする」というHTMLタグ
 * └─ style= の中にCSSを直接書いている（インラインスタイルという）
 *
 * @enderror
 * └─ @error ブロックの終わり
 * └─ @if の @endif と同じ役割
 *
 * ──────────────────────────────────────────
 * 【全体の流れをまとめると】
 * ──────────────────────────────────────────
 *
 * <input name="category" value="{{ old('category') }}">  ← 入力欄（前の値を保持）
 * @error('category')      ← 'category' にエラーがあれば...
 *     <span style="color: red;">{{ $message }}</span>  ← 赤字でエラーを表示
 * @enderror               ← @error終わり
 */

/**
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * ✅ 【3. なぜ2種類の表示方法を使うのか？】
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 *
 * 今回のBladeには「全件まとめ表示」と「項目ごと表示」の2種類があります。
 * なぜ両方使うのでしょうか？
 *
 * 【全件まとめ表示（フォームの上部）】
 * @if ($errors->any())
 *     @foreach ($errors->all() as $error) ...
 *
 * → 役割：「何が間違っているか、全部まとめて知らせる」
 * → メリット：スクロールしなくても全エラーが一目でわかる
 * → 例：エラーが3つあれば3つ全部上に表示される
 *
 * 【項目ごと表示（各入力欄の横）】
 * @error('category')
 *     <span>{{ $message }}</span>
 *
 * → 役割：「どの入力欄を直せばいいか、その場で知らせる」
 * → メリット：入力欄のすぐ横にエラーが出るので迷わない
 * → 例：カテゴリ欄の横に「category は必須項目です。」と出る
 *
 * 【たとえ話】
 * テスト用紙に例えると...
 * 全件まとめ = 表紙に「Q1・Q3・Q5が間違い」と書いてある
 * 項目ごと  = 各問題の横に「ここが間違い」と書いてある
 * 両方あると、どこを直せばいいかが一番わかりやすい。
 */

/**
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * ✅ 【4. 最小構成から自分で組み立てる練習】
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 *
 * 「読んでわかる」と「自分で書ける」は別物です。
 * 最小構成から手を動かして組み立てる練習をしましょう。
 *
 * 【ステップ1：まず1項目だけのフォームを作る】
 *
 * 以下は「カテゴリだけ」のシンプルなフォームです。
 * これが基本形です。
 *
 * <form method="POST" action="{{ route('expenses.store') }}">
 *     @csrf
 *     <input type="text" name="category">
 *     <button type="submit">送信</button>
 * </form>
 *
 * 【ステップ2：old() を追加する】
 *
 * 入力欄に old() を追加するだけで、前の値が保持されます。
 * value属性に old('項目名') を入れるだけです。
 *
 * 変更前：<input type="text" name="category">
 * 変更後：<input type="text" name="category" value="{{ old('category') }}">
 *
 * たった1箇所追加するだけ。難しくないですね。
 *
 * 【ステップ3：@error を追加する】
 *
 * 入力欄の直下に @error〜@enderror を追加します。
 *
 * <input type="text" name="category" value="{{ old('category') }}">
 * @error('category')
 *     <span style="color: red;">{{ $message }}</span>
 * @enderror
 *
 * パターンは毎回同じです：
 * @error('入力欄のname属性と同じ名前')
 *     <span>{{ $message }}</span>
 * @enderror
 *
 * 【ステップ4：全件まとめ表示を追加する】
 *
 * フォームの上に @if〜@endif のブロックを追加します。
 * これはフォームの中ではなく、フォームの上に置きます。
 *
 * @if ($errors->any())
 *     <ul>
 *         @foreach ($errors->all() as $error)
 *             <li>{{ $error }}</li>
 *         @endforeach
 *     </ul>
 * @endif
 *
 * <form method="POST" ...>
 *     ← ここから下がフォーム
 * </form>
 *
 * 【まとめ：組み立ての順番】
 * 1. まずフォームを作る（@csrf、input、button）
 * 2. inputに value="{{ old('名前') }}" を追加
 * 3. inputの下に @error〜@enderror を追加
 * 4. フォームの上に @if ($errors->any())〜@endif を追加
 *
 * この順番で考えると、難しくありません。
 */

