/**
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 🧠 【今日学ぶこと】
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 *
 * 今まで「開発環境（ローカル）」でLaravelを動かしてきました。
 * GodeVenを実際にインターネットに公開するには「本番環境」用の設定が必要です。
 *
 * 【開発環境と本番環境の違い】
 *
 * 開発環境（ローカル）：
 * - 自分のパソコンの中だけで動く
 * - エラーが出たら詳細情報を画面に表示してOK（自分しか見ない）
 * - 設定は適当でも動く
 *
 * 本番環境（サーバー公開）：
 * - インターネット上で誰でもアクセスできる
 * - エラーの詳細を画面に出してはいけない（攻撃者に情報を与えてしまう）
 * - 設定を間違えると = セキュリティホールになる
 *
 * 【たとえ話：お店の「開店前」と「開店後」】
 *
 * 開発環境 = 開店前の準備中
 * → スタッフしかいないので、散らかしたまま作業してOK
 * → 「ここの配線が壊れてる！」と大声で言ってもOK
 *
 * 本番環境 = お客さんが来ているお店
 * → お客さんに見られても恥ずかしくない状態にする
 * → 「レジが壊れた」とお客さんの前で叫ぶのはNG（「少々お待ちください」と言う）
 *
 * 【今日学ぶ4つのこと】
 * 1. .env（ドットエンブ）ファイルの役割
 * 2. APP_DEBUG（アップデバッグ）をオフにする理由
 * 3. APP_ENV（アップエンブ）をproductionに変える意味
 * 4. キャッシュ（高速化）コマンド
 */

/**
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * ✅ 【1. .env ファイルとは？】
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 *
 * .env（ドットエンブ） = 「環境設定ファイル」
 *
 * 【たとえ話】
 * アプリの「設定メモ帳」です。
 * 「どのデータベースに接続するか」「デバッグモードはオンかオフか」など、
 * 環境によって変わる設定をまとめて書いておく場所です。
 *
 * 【.envの特徴】
 * - Gitにコミットされない（.gitignoreで除外済み）
 *   → パスワードなどの秘密情報を安全に管理できる
 * - 開発環境用と本番環境用で内容が変わる
 *   → 同じアプリでも「場所」によって設定を切り替えられる
 *
 * 【KOHさんの現在の.envの状態（抜粋）】
 *
 * APP_NAME=Laravel          ← アプリの名前
 * APP_ENV=local             ← 「今は開発環境です」という宣言
 * APP_KEY=（長い文字列）     ← 暗号化に使う鍵（絶対に外部に出さない）
 * APP_DEBUG=true            ← デバッグモード：オン（エラー詳細を表示）
 * APP_URL=http://localhost:8080  ← アプリのURL
 *
 * DB_CONNECTION=mysql
 * DB_HOST=mysql             ← DockerのMySQLコンテナ名
 * DB_PORT=3306
 * DB_DATABASE=laravel_db
 * DB_USERNAME=laravel_user
 * DB_PASSWORD=（パスワード）
 */

/**
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * ✅ 【2. APP_DEBUG（アップデバッグ）の意味】
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 *
 * APP_DEBUG（アップデバッグ） = 「エラーの詳細を画面に出すかどうか」のスイッチ
 *
 * 【APP_DEBUG=true（オン）の場合】
 * エラーが起きると → こんな画面が出る：
 * 「エラー：データベースに接続できません。
 *  ホスト名：mysql、ユーザー名：laravel_user、
 *  ファイル：/var/www/html/app/Http/Controllers/ExpenseController.php
 *  行番号：45」
 *
 * → 開発中は便利（どこで何が起きたかわかる）
 * → 本番では危険（攻撃者にサーバーの内部構造を教えてしまう）
 *
 * 【APP_DEBUG=false（オフ）の場合】
 * エラーが起きると → シンプルなエラーページだけ表示：
 * 「500 Server Error」
 *
 * → 詳細は表示しない（ユーザーには見えない）
 * → エラーの詳細はサーバーのログファイルに記録される
 *
 * 【本番環境では必ずfalseにする】
 * APP_DEBUG=false
 *
 * これはLaravel開発者全員が守るルールです。
 */

/**
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * ✅ 【3. APP_ENV（アップエンブ）の意味】
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 *
 * APP_ENV（アップエンブ） = 「今どの環境で動いているか」の宣言
 *
 * 【値の種類と意味】
 *
 * APP_ENV=local
 * → 「今は開発環境です」
 * → 自分のパソコン（Docker）で動かしている状態
 *
 * APP_ENV=production（プロダクション）
 * → 「今は本番環境です」
 * → 実際のサーバー（Xserver VPS等）で動かしている状態
 *
 * APP_ENV=testing（テスティング）
 * → 「今はテスト環境です」
 * → Step10-01・02でやったPHPUnitのテスト実行時
 *
 * 【本番環境では】
 * APP_ENV=production
 *
 * 【なぜ宣言が必要か】
 * Laravelの一部の機能は「APP_ENVがproductionか否か」で動作が変わります。
 * 例：キャッシュの挙動、エラーハンドリングの詳細度など
 */

/**
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * ✅ 【4. 本番用.envの変更箇所まとめ】
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 *
 * 開発環境（今の状態）→ 本番環境（変更後）
 *
 * ─────────────────────────────────────────
 * 変更前（開発環境）：
 *
 * APP_ENV=local
 * APP_DEBUG=true
 * APP_URL=http://localhost:8080
 *
 * ─────────────────────────────────────────
 * 変更後（本番環境）：
 *
 * APP_ENV=production
 * APP_DEBUG=false
 * APP_URL=https://あなたのドメイン名
 *
 * ─────────────────────────────────────────
 *
 * 【重要】今のlaravel-practiceの.envは変更しません
 * Step10-03は「本番環境ではこう設定する」という知識を学ぶステップです。
 * 実際の変更はGodeVenをXserver VPSにデプロイするときに行います。
 *
 * 【DB設定も本番では変わる】
 *
 * 開発環境（Docker）：
 * DB_HOST=mysql          ← Dockerのコンテナ名
 * DB_DATABASE=laravel_db
 * DB_USERNAME=laravel_user
 *
 * 本番環境（Xserver VPS）：
 * DB_HOST=localhost      ← 同じサーバー内のMySQLに直接接続
 * DB_DATABASE=本番用DB名
 * DB_USERNAME=本番用ユーザー名
 */

/**
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * ✅ 【5. キャッシュ（高速化）コマンド】
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 *
 * キャッシュ（Cache）= 「事前に準備しておくこと」
 *
 * 【たとえ話】
 * レストランの「仕込み」と同じです。
 * 注文が来てから野菜を切り始めると時間がかかります。
 * 開店前に野菜を切っておけば、注文が来たらすぐ出せます。
 *
 * Laravelも同じで、設定ファイルやルーティング情報を
 * 事前にまとめてキャッシュしておくと、アプリの動作が速くなります。
 *
 * 【本番環境でよく使う3つのコマンド】
 *
 * ① 設定ファイルのキャッシュ
 *    .envや各種configファイルを1つにまとめて高速化
 *
 * ② ルーティングのキャッシュ
 *    web.php等のルート設定を事前にまとめて高速化
 *
 * ③ Bladeテンプレートのキャッシュ
 *    Bladeファイルをコンパイルして高速化
 *
 * 【実際のコマンド】
 */

