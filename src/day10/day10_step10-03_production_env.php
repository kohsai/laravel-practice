/**
 * ① 設定キャッシュ（本番環境のサーバーで実行するコマンド）：
 * docker compose exec php php artisan config:cache
 *
 * ② ルートキャッシュ（本番環境のサーバーで実行するコマンド）：
 * docker compose exec php php artisan route:cache
 *
 * ③ Bladeキャッシュ（本番環境のサーバーで実行するコマンド）：
 * docker compose exec php php artisan view:cache
 *
 * 【まとめて実行するコマンド（本番環境）：
 * docker compose exec php php artisan optimize
 *
 * ─────────────────────────────────────────
 *
 * 【重要：開発中はキャッシュをクリアする】
 *
 * 本番環境でキャッシュした後、コードを変更した場合は
 * キャッシュをクリアしないと変更が反映されません。
 *
 * キャッシュをクリアするコマンド（本番環境で変更を反映するとき）：
 * docker compose exec php php artisan optimize:clear
 *
 * ─────────────────────────────────────────
 *
 * 【開発環境（今のlaravel-practice）ではキャッシュ不要】
 *
 * 理由：
 * - キャッシュすると、.envを変更してもすぐ反映されない
 * - 開発中はコードを頻繁に変更するため、キャッシュは邪魔になる
 * - Dockerのローカル環境は速度より利便性を優先
 *
 * 本番環境（Xserver VPS）に初めてデプロイするときに使うコマンドです。
 */

/**
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * ✅ 【6. APP_KEY（アップキー）について】
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 *
 * APP_KEY（アップキー） = 「暗号化の鍵」
 *
 * 【たとえ話】
 * 家の鍵と同じです。
 * Laravelはセッション情報やクッキーを「暗号化」して保存します。
 * APP_KEYはその暗号化・複合化に使う鍵です。
 *
 * 【特徴】
 * - `php artisan key:generate` で自動生成される
 * - 一度設定したら基本的に変更しない
 * - 絶対に外部に公開しない（GitHubにアップしない）
 * - .gitignoreで.envごと除外されているので通常は安全
 *
 * 【本番環境でのAPP_KEY】
 *
 * 本番サーバーに初めてLaravelをセットアップするとき、
 * .envファイルをサーバーに作成してからこのコマンドを実行します：
 *
 * php artisan key:generate
 *
 * → APP_KEYが自動生成されて.envに書き込まれます
 * → 開発環境のAPP_KEYをそのままコピーしてもOKですが、
 *   本番用に新しく生成する方が望ましいです
 *
 * 【注意】
 * 本番稼働中にAPP_KEYを変更すると、既存のセッションがすべて無効になります。
 * ログイン中のユーザーが全員ログアウトされます。
 * 本番稼働後は変更しないようにします。
 */

/**
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * ✅ 【7. .env.example（ドットエンブドットエグザンプル）とは？】
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 *
 * .env.example = 「.envのサンプルファイル」
 *
 * 【特徴】
 * - Gitにコミットされる（.gitignoreで除外されていない）
 * - パスワードやAPP_KEYは空欄にしておく
 * - 「この項目が必要ですよ」というテンプレートとして使う
 *
 * 【使い方の流れ】
 * 新しいサーバーにLaravelをセットアップするとき：
 *
 * 1. GitHubからコードをcloneする
 * 2. .env.exampleをコピーして.envを作る
 * 3. .envの中身を本番環境用に書き換える
 * 4. php artisan key:generate でAPP_KEYを生成する
 *
 * 【KOHさんのプロジェクトの.env.exampleの確認方法】
 * VSCodeのエクスプローラーで src/laravel/.env.example を開いてみてください。
 * 現在の.envと比較すると構造が同じで、秘密情報が空欄になっているのがわかります。
 */

/**
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * ✅ 【8. 本番環境設定のチェックリスト】
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 *
 * GodeVenをXserver VPSにデプロイするときに使うチェックリストです。
 * 今は「こういうものがある」と把握するだけでOKです。
 *
 * ─────────────────────────────────────────
 * □ APP_ENV=production に変更済みか
 * □ APP_DEBUG=false に変更済みか
 * □ APP_URL=https://本番ドメイン に変更済みか
 * □ APP_KEY が設定済みか（key:generate 実行済みか）
 * □ DB_HOST・DB_DATABASE・DB_USERNAMEが本番用に変更済みか
 * □ DB_PASSWORD が本番用に設定済みか
 * □ MAIL設定が本番用になっているか（必要な場合）
 * □ php artisan migrate が本番DBに対して実行済みか
 * □ php artisan optimize が実行済みか
 * □ ストレージのシンボリックリンクが作成済みか（画像アップロードを使う場合）
 *   → php artisan storage:link
 * ─────────────────────────────────────────
 *
 * このチェックリストはStep10-04（デプロイ準備）でさらに詳しく学びます。
 */

/**
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 🧩 【Q&A：よくある質問】
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 *
 * Q1. .envファイルはGitにコミットしてはいけないんですか？
 *
 * A1. してはいけません。
 *     理由は「パスワードやAPP_KEYなどの秘密情報が含まれているから」です。
 *     GitHubはインターネット上に公開されるので、
 *     .envをコミットするとパスワードが世界中に公開されることになります。
 *
 *     Laravelの.gitignoreには最初から .env が含まれているので、
 *     通常は自動的に除外されます。
 *     `git status` を実行しても .env は表示されません。
 *
 * Q2. APP_DEBUG=trueのまま本番に出すとどうなりますか？
 *
 * A2. 深刻なセキュリティリスクになります。
 *     エラーが起きるたびに、サーバーのファイルパス・DB設定・
 *     コードの内容などが画面に表示されてしまいます。
 *     悪意のある人にとって、攻撃の手がかりになります。
 *
 * Q3. キャッシュコマンドを開発環境で実行したらどうなりますか？
 *
 * A3. .envを変更しても反映されなくなります。
 *     設定をキャッシュしてしまうと、.envを書き換えても
 *     「古いキャッシュの設定」が使われ続けます。
 *     開発中にやってしまった場合は optimize:clear で解除できます。
 *
 * Q4. APP_ENVをproductionにするとLaravelの何が変わりますか？
 *
 * A4. 主に以下が変わります：
 *     - エラーの詳細表示がオフになる（APP_DEBUGと連動）
 *     - 一部のデバッグ用ツール（Laravel Telescope等）が無効になる
 *     - キャッシュの動作が最適化される
 *     - 「本番モード確認」が必要なコマンドで確認メッセージが出る
 *       例：php artisan migrate を本番で実行すると「本当にいいですか？」と聞かれる
 *
 * Q5. 開発環境のAPP_KEYを本番にそのままコピーしてもいいですか？
 *
 * A5. 技術的には動きますが、望ましくありません。
 *     開発中にAPP_KEYをチームメンバーに共有していたり、
 *     誤ってGitにコミットした場合に、本番環境にも影響が出るリスクがあります。
 *     本番環境では key:generate で新しく生成する方が安全です。
 *
 * Q6. .envと.env.exampleの使い分けを一言で言うと？
 *
 * A6. .env = 「実際に使う設定メモ帳」（秘密情報あり・Gitに入れない）
 *     .env.example = 「空欄のテンプレート」（秘密情報なし・Gitに入れる）
 */

/**
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 📝 【用語集】
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 *
 * .env（ドットエンブ）
 * └─ 環境設定ファイル。パスワードやURLなど環境ごとに変わる設定を書く
 *
 * APP_ENV（アップエンブ）
 * └─ 「今どの環境で動いているか」の宣言。local / production / testing
 *
 * APP_DEBUG（アップデバッグ）
 * └─ エラー詳細を画面に出すかどうかのスイッチ。本番はfalse必須
 *
 * APP_KEY（アップキー）
 * └─ 暗号化に使う鍵。外部に出してはいけない。php artisan key:generate で生成
 *
 * APP_URL（アップユーアールエル）
 * └─ アプリのURL。開発はlocalhost、本番は実際のドメイン名
 *
 * キャッシュ（Cache）
 * └─ 設定やルートを事前にまとめて高速化すること。本番環境で使う
 *
 * optimize（オプティマイズ）
