/**
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 🧠 【今日学ぶこと】
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 *
 * 「デプロイ（デプロイ）」とは、作ったアプリをインターネット上に公開することです。
 *
 * 【たとえ話】
 * 今のKOHさんの環境はこんな状態です：
 *
 * ┌─────────────────────────────────────────────┐
 * │  KOHさんのパソコン（ローカル環境）              │
 * │                                             │
 * │  Docker → Laravel → ブラウザで確認できる      │
 * │                                             │
 * │  ※ 自分のパソコンだけで動いている             │
 * │  ※ 他の人はアクセスできない                  │
 * └─────────────────────────────────────────────┘
 *
 * デプロイ後はこうなります：
 *
 * ┌─────────────────────────────────────────────┐
 * │  Xserver VPS（インターネット上のサーバー）      │
 * │                                             │
 * │  Docker → Laravel → 世界中からアクセス可能    │
 * │                                             │
 * │  ※ https://stemme.example.com のように       │
 * │     URLでアクセスできる                      │
 * └─────────────────────────────────────────────┘
 *
 * 【今日の目標】
 * 「ローカルで動いているLaravelをVPSで公開するまでに、
 *  何をどの順番でやるのか」を頭に入れる。
 * 実際の操作はGodeVen開発開始時に行います。
 */

/**
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * ✅ 【1. ローカル環境と本番環境の違い】
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 *
 * 「ローカル（ローカル）」= 自分のパソコンの中だけで動く環境
 * 「本番（ほんばん）」   = インターネット上で実際に公開されている環境
 *
 * ┌──────────────────┬────────────────────────────────┐
 * │                  │ ローカル環境      │ 本番環境（VPS）  │
 * ├──────────────────┼───────────────────┼───────────────┤
 * │ 誰がアクセスできる │ 自分だけ          │ 世界中の誰でも  │
 * │ URLの形           │ localhost:8080    │ https://○○.com │
 * │ APP_DEBUG         │ true（エラー全表示）│ false（非表示） │
 * │ APP_ENV           │ local             │ production      │
 * │ DB                │ Dockerのmysql     │ VPS上のmysql    │
 * │ 目的              │ 開発・確認        │ 実際のサービス  │
 * └──────────────────┴───────────────────┴───────────────┘
 *
 * 【重要】
 * Step10-03で学んだ「APP_DEBUG=false」「APP_ENV=production」への変更は、
 * この「本番環境」に切り替えるための設定です。
 */

/**
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * ✅ 【2. Xserver VPS とは何か】
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 *
 * VPS（ブイピーエス）= Virtual Private Server（バーチャル・プライベート・サーバー）
 * 直訳：仮想・専用・サーバー
 *
 * 【たとえ話】
 * - レンタルサーバー（さくら・エックスサーバー等）= マンションの一室を借りる
 *   → 壁や配管は共有。間取りを変えられない
 *
 * - VPS = 一軒家を借りる
 *   → 自分専用。Dockerも自由に入れられる。自由度が高い
 *
 * - 専用サーバー = 土地を買って家を建てる
 *   → 最高の自由度だが高価
 *
 * GodeVenにはVPSが最適です：
 * ✅ Dockerが使える（ローカル環境と同じ構成で動かせる）
 * ✅ 月額1,000円程度から借りられる
 * ✅ Xserverなら日本語サポートがある
 *
 * 【Xserver VPSの基本情報】
 * - 公式サイト：https://vps.xserver.ne.jp/
 * - 最小プラン：月額830円〜（2GBメモリ）
 * - GodeVen MVP段階では2GBプランで十分
 * - 契約はDay10完了後・GodeVen開発開始時に行う
 */

/**
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * ✅ 【3. SSH とは何か】
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 *
 * SSH（エスエスエイチ）= Secure Shell（セキュア・シェル）
 * 直訳：安全な・操作画面
 *
 * 【たとえ話】
 * SSHは「インターネット越しにVPSのターミナルを操作する」ための仕組みです。
 *
 * 今のKOHさんの操作：
 * KOHさんのキーボード → 自分のパソコンのターミナル → Dockerの中
 *
 * SSH接続後の操作：
 * KOHさんのキーボード → 自分のパソコンのターミナル → （インターネット）→ VPSのターミナル
 *
 * VPSを契約したら、ターミナルでこのコマンドを打ちます：
 *
 * 【SSHコマンドのイメージ】（※実際の操作はGodeVen開始時）
 *   ssh root@111.222.333.444
 *
 *   ↑ 「111.222.333.444」はVPSのIPアドレス（インターネット上の住所）
 *
 * 接続成功すると、VPSのターミナルが開き、
 * docker コマンドや git コマンドをVPS上で実行できるようになります。
 *
 * 【セキュリティの仕組み】
 * SSHには「鍵認証（かぎにんしょう）」という仕組みがあります。
 * 「公開鍵（こうかいかぎ）」と「秘密鍵（ひみつかぎ）」のペアを使い、
 * 正しい鍵を持っている人だけがVPSに入れます。
 * パスワードより安全です。
 *
 * ※ 鍵の作成・設定もGodeVen開始時に一緒に行います。
 */

/**
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * ✅ 【4. デプロイの全体ステップ（地図）】
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 *
 * GodeVen（Stemme等）をXserver VPSで公開するまでの全体の流れです。
 * 今は「こういう順番でやるんだ」と把握するだけでOKです。
 *
 * ━━━━━━━━━━━━━━━━━━━━
 * フェーズ1：VPSの準備
 * ━━━━━━━━━━━━━━━━━━━━
 *
 * STEP 1. Xserver VPSを契約する
 *         → 公式サイトでプランを選んで申し込む（月額830円〜）
 *
 * STEP 2. SSHで接続できるようにする
 *         → 「鍵認証」の設定を行う
 *         → ターミナルで ssh root@VPSのIPアドレス でつながることを確認
 *
 * STEP 3. VPSにDockerをインストールする
 *         → VPSのターミナルで docker --version が動けばOK
 *         → ローカルと同じDocker環境をVPS上に作る
 *
 * STEP 4. ドメインを取得・設定する（任意）
 *         → 「stemme.jp」のような独自ドメインを取得する
 *         → XserverのDNS設定でVPSのIPアドレスと紐付ける
 *         → ドメインなしでもIPアドレスでアクセスは可能（開発初期はこれでOK）
 *
 * ━━━━━━━━━━━━━━━━━━━━
 * フェーズ2：Laravelのデプロイ
 * ━━━━━━━━━━━━━━━━━━━━
 *
 * STEP 5. VPS上にGitHubからコードを持ってくる
 *         → VPSのターミナルで git clone を実行
 *         → GodeVenのリポジトリをVPS上に展開する
 *
 * STEP 6. VPS上の .env を本番用に設定する
 *         → Step10-03で学んだ内容を本番環境に適用する
 *         → APP_ENV=production
 *         → APP_DEBUG=false
 *         → DB_PASSWORD=（本番用の強いパスワード）
 *
 * STEP 7. Dockerを起動する
 *         → docker compose up -d
 *         → Laravelが動き始める
 *
 * STEP 8. Laravelの初期設定コマンドを実行する
 *         → docker compose exec php php artisan key:generate
 *         → docker compose exec php php artisan migrate
 *         → docker compose exec php php artisan config:cache
 *         → docker compose exec php php artisan route:cache
 *
 * STEP 9. ブラウザで動作確認する
 *         → http://VPSのIPアドレス でアクセスできればデプロイ成功
 *
 * ━━━━━━━━━━━━━━━━━━━━
 * フェーズ3：HTTPS化（任意・推奨）
 * ━━━━━━━━━━━━━━━━━━━━
 *
 * STEP 10. SSL証明書を取得する
 *          → Let's Encrypt（レッツエンクリプト）という無料のサービスを使う
 *          → http:// → https:// に変わる
 *          → ユーザーの情報が暗号化されて安全になる
 *          → 本番サービスでは必須
 *
 * 【全体の流れまとめ】
 *
 *  VPS契約 → SSH設定 → Docker導入 → ドメイン設定
 *      ↓
 *  git clone → .env設定 → Docker起動 → artisanコマンド
 *      ↓
 *  動作確認 → HTTPS化 → 公開完了！
 */

/**
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * ✅ 【5. GodeVen開発前・本番公開チェックリスト】
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 *
 * Step10-03の議論で決定した内容です。
 * GodeVenをVPSで公開する直前に、このチェックリストを使います。
 *
 * ━━━━━━━━━━━━━━━━
 * 【セキュリティ設定】
 * ━━━━━━━━━━━━━━━━
 *
 * □ APP_ENV=production に変更した
 * □ APP_DEBUG=false に変更した
 * □ APP_KEY が設定されている（php artisan key:generate 済み）
 * □ DB_PASSWORD を強いパスワードに変更した
 * □ .env が .gitignore に含まれている（GitHubに上がっていない）
 * □ storage/ と bootstrap/cache/ の権限を設定した
 *
 * ━━━━━━━━━━━━━━━━
 * 【Laravelの設定】
 * ━━━━━━━━━━━━━━━━
 *
 * □ php artisan config:cache を実行した
 * □ php artisan route:cache を実行した
 * □ php artisan migrate を実行した（本番DBにテーブルが作られた）
 * □ php artisan storage:link を実行した（画像アップロード機能がある場合）
 *
 * ━━━━━━━━━━━━━━━━
 * 【Gitとファイル管理】
 * ━━━━━━━━━━━━━━━━
 *
 * □ .env が GitHub に上がっていないことを確認した
 * □ 不要なファイル（デバッグ用コード等）を削除した
 * □ main ブランチが最新の状態になっている
 *
 * ━━━━━━━━━━━━━━━━
 * 【バージョン確認】
 * ━━━━━━━━━━━━━━━━
 *
 * □ PHP のバージョンを確認した
 *   確認コマンド（ローカル）：docker compose exec php php --version
 *
 * □ Laravel のバージョンを確認した
 *   確認コマンド（ローカル）：docker compose exec php php artisan --version
 *
 * □ MySQL のバージョンを確認した
 *   確認コマンド（ローカル）：docker compose exec mysql mysql --version
 *
 * □ Composer パッケージに重大な脆弱性がないか確認した
 *   確認コマンド（ローカル）：docker compose exec php composer audit
 *
 * □ Node.js / npm のバージョンを確認した（フロントエンドがある場合）
 *   確認コマンド（ローカル）：node --version && npm --version
 *
 * ━━━━━━━━━━━━━━━━
 * 【動作確認】
 * ━━━━━━━━━━━━━━━━
 *
 * □ ローカルで全機能が正常に動作することを確認した
 * □ ブラウザのエラーコンソールにエラーが出ていないことを確認した
 * □ VPS上で http:// でアクセスできることを確認した
 * □ HTTPS化が完了したことを確認した（本番公開前に必須）
 */

/**
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * ✅ 【6. よくある質問（Q&A）】
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 *
 * Q1. ローカルのDockerと本番のDockerは同じですか？
 *
 * A1. 基本的に同じです。
 *     docker-compose.yml をVPS上でも使うので、
 *     ローカルと同じ構成（PHP・MySQL・nginx等）で動かせます。
 *     「ローカルで動けば本番でも動く」というのがDockerの大きな利点です。
 *
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 *
 * Q2. GodeVenを公開するとき、laravel-practiceのコードも公開されますか？
 *
 * A2. されません。
 *     GodeVen（Stemme等）は新しいLaravelプロジェクトとして
 *     別のGitHubリポジトリで管理します。
 *     laravel-practiceはあくまでも学習用で、公開する必要はありません。
 *
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 *
 * Q3. VPSを止めたらGodeVenにアクセスできなくなりますか？
 *
 * A3. はい、アクセスできなくなります。
 *     VPSはレンタルなので、契約を止めるとサーバーが消えます。
 *     ただしコードはGitHubに残るので、
 *     別のVPSを借りて同じ手順でデプロイすれば復活できます。
 *
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 *
 * Q4. データベースのデータはどこに保存されますか？
 *
 * A4. VPS上のDockerボリューム（どっかーぼりゅーむ）に保存されます。
 *     VPSを再起動してもデータは消えませんが、
 *     VPSを解約するとデータも消えます。
 *     重要なデータは定期的にバックアップが必要です。
 *     （GodeVen開発時にバックアップ方法も確認します）
 *
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 *
 * Q5. デプロイにかかる時間はどのくらいですか？
 *
 * A5. 初回は2〜3時間を見てください。
 *     VPS契約・SSH設定・Docker導入・ドメイン設定・HTTPS化と
 *     手順が多いです。Klogeが全ステップで一緒にサポートします。
 *     2回目以降（コードの更新デプロイ）は10〜15分程度です。
 *
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 *
 * Q6. コードを更新したとき、本番にはどうやって反映しますか？
 *
 * A6. ローカルで変更 → GitHubにpush → VPSで git pull → 完了
 *
 *     具体的な手順のイメージ（実際の操作はGodeVen開始時）：
 *
 *     ローカルで作業後：
 *     git push origin main
 *
 *     VPSのターミナルで：
 *     cd ~/godevenプロジェクト
 *     git pull origin main
 *     docker compose exec php php artisan migrate  （DBの変更がある場合）
 *     docker compose exec php php artisan config:cache
 *
 *     これだけで本番に最新版が反映されます。
 */

