 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * ✅ 【4. 既存のサンプルテストを実行してみる】
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 *
 * SQLiteの設定が完了したら、まず既存のサンプルテストを実行してみましょう。
 */

