// docker compose exec php php artisan test

/**
 * 【成功時の表示例】
 *
 *    PASS  Tests\Unit\ExampleTest
 *   ✓ that true is true                          0.01s
 *
 *    PASS  Tests\Feature\ExampleTest
 *   ✓ the application returns a successful response  0.05s
 *
 *   Tests:  2 passed (2 assertions)
 *   Duration: 0.10s
 *
 * 【結果の読み方】
 *
 * PASS（パス） = テスト成功（緑色）
 * FAIL（フェイル） = テスト失敗（赤色）
 * ✓ = 1つのテストが成功
 * Tests: 2 passed = 合計2つのテストが全部成功
 *
 * 【失敗時の例】
 *
 *    FAIL  Tests\Feature\ExampleTest
 *   ✕ the application returns a successful response
 *   Expected status code 200 but received 500.
 *
 * → 「200を期待したけど500が返ってきた」という意味
 * → アプリにエラーがある、ということが分かる
 */

/**
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * ✅ 【5. RefreshDatabaseトレイトについて】
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 *
 * Step10-02でテストを書くときに必ず使う機能なので、先に説明します。
 *
 * 【RefreshDatabase（リフレッシュ・データベース）とは？】
 *
 * テストのたびにDBを綺麗な状態にリセットしてくれる機能です。
 *
 * たとえ話：
 * 料理の練習をするたびに「まな板をきれいに洗い直す」イメージ。
 * 前のテストのデータが残っていると次のテストに影響するので、
 * 毎回きれいな状態から始めます。
 *
 * 【使い方】
 *
 * テストクラスの中に以下の1行を追加するだけです：
 *
 * use RefreshDatabase;
 *
 * 書く場所：
 *
 * class ExpenseTest extends TestCase
 * {
 *     use RefreshDatabase;  ← ここに書く
 *
 *     public function test_〇〇(): void
 *     {
 *         // ...
 *     }
 * }
 *
 * 【動作の仕組み】
 *
 * 1. テスト開始前にマイグレーション（テーブル作成）を自動実行
 * 2. テスト実行
 * 3. テスト終了後にDBを全部消去
 * 4. 次のテスト前に再びマイグレーション実行
 *
 * SQLiteの「:memory:」と組み合わせることで、
 * 毎回きれいなDBでテストができます。
 */

/**
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 📝 【Q&A】
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 *
 * Q1. テストを書かないとどうなりますか？
 *
 * A1. 機能が増えるにつれて「前の機能が壊れていないか」の確認が大変になります。
 *     特にGodeVenのように複数機能を持つアプリでは、
 *     新機能を追加するたびに全部手動確認するのは現実的ではありません。
 *     テストコードがあれば「docker compose exec php php artisan test」
 *     の1コマンドで全部確認できます。
 *
 * Q2. UnitテストとFeatureテストはどちらを書けばいいですか？
 *
 * A2. GodeVenのような個人開発MVPでは、まずFeatureテストから始めるのが現実的です。
 *     「ユーザーが実際に使う操作」を直接テストできるので、
 *     少ないテスト数で大きな安心感が得られます。
 *     Unitテストは「複雑な計算ロジック」などが出てきたときに書き始めれば十分です。
 *
 * Q3. assertStatus(200)の200って何ですか？
 *
 * A3. HTTPステータスコード（エイチティーティーピー・ステータス・コード）といいます。
 *     サーバーが「どういう状態か」を数字で表したものです。
 *     よく使う番号：
 *     - 200 = OK（正常）
 *     - 302 = Found（リダイレクト）
 *     - 404 = Not Found（ページが見つからない）
 *     - 500 = Internal Server Error（サーバーエラー）
 *
 * Q4. テストファイルはどこに保存しますか？
 *
 * A4. tests/Feature/ フォルダです。
 *     ファイル名の末尾は必ず「Test.php」にします。
 *     例：ExpenseTest.php、TaskTest.php
 *     「Test」が末尾にないとPHPUnitがテストファイルと認識しません。
 *
 * Q5. テストは本番環境でも動きますか？
 *
 * A5. 動きますが、絶対にやってはいけません。
 *     テストはAPP_ENV=testingの環境でのみ実行します。
 *     phpunit.xmlに「APP_ENV=testing」が設定されているので、
 *     「docker compose exec php php artisan test」で実行する限り安全です。
 */

/**
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 📚 【用語集】
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 *
 * PHPUnit（ピーエイチピーユニット）
 * └─ PHPのテストを実行するツール。Laravelに最初から入っている。
 *
 * Unit Test（ユニットテスト）
 * └─ 部品単体のテスト。関数やメソッド1つを確認する。
 *
 * Feature Test（フィーチャーテスト）
 * └─ 機能全体のテスト。HTTP リクエストからDBまで一気に確認する。
 *
 * assert（アサート）
 * └─ 「〜であることを確認する」という意味。テストの核心部分。
 *
 * SQLite（エスキューライト）
 * └─ 超軽量のDB。テスト用に「メモリ上だけで動く使い捨てDB」として使う。
 *
 * :memory:（メモリ）
 * └─ SQLiteをメモリ上だけに作る指定。テスト終了後に自動消去される。
 *
 * RefreshDatabase（リフレッシュデータベース）
 * └─ テストのたびにDBをリセットしてくれるトレイト（機能パーツ）。
 *
 * HTTPステータスコード
 * └─ サーバーの状態を表す数字。200=OK、302=リダイレクト、404=Not Found。
 *
 * PASS（パス）
 * └─ テスト成功。期待通りの動作をしている。
 *
 * FAIL（フェイル）
 * └─ テスト失敗。期待と違う動作をしている。
 */
