/**
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 🧠 【今日学ぶこと】
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 *
 * 【テストって何？】
 *
 * アプリを作ったあと「ちゃんと動くか確認する」作業をテストといいます。
 * 今まではブラウザで手動確認していましたが、
 * それを「自動でやってくれるプログラム」がテストコードです。
 *
 * 【たとえ話：工場の検品ライン】
 *
 * 工場で商品を作ったあと、「正しく動くか」を検品しますよね。
 * - 手動検品 = 人間が1つ1つ確認する（今まで）
 * - 自動検品 = 機械が全部チェックしてくれる（テストコード）
 *
 * 機能が増えるほど手動確認は大変になります。
 * テストコードがあれば、コマンド1つで全部チェックできます。
 *
 * 【今日学ぶ5つのこと】
 * 1. テストの種類（Unit・Feature）
 * 2. テスト用DBの準備（SQLite設定）
 * 3. テストファイルの構造
 * 4. 実際にテストを書いて実行する
 * 5. テスト結果の読み方
 */

/**
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * ✅ 【1. テストの種類】
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 *
 * Laravelには大きく2種類のテストがあります。
 *
 * ┌─────────────────────────────────────────────────────┐
 * │ Unit Test（ユニットテスト）= 部品単体のテスト           │
 * │ Feature Test（フィーチャーテスト）= 機能全体のテスト     │
 * └─────────────────────────────────────────────────────┘
 *
 * 【Unit Test（ユニット・テスト）】
 * - 「unit」= 「単体・部品」という意味
 * - 関数やメソッドなど、小さな部品1つだけをテストする
 * - DBやHTTPリクエストを使わない
 *
 * たとえ話：車のエンジンだけを取り出してテストする感じ
 *
 * 例：「金額を計算するメソッドが正しい結果を返すか？」
 *
 * 【Feature Test（フィーチャー・テスト）】
 * - 「feature」= 「機能」という意味
 * - 実際にHTTPリクエストを送って、画面・DBまで含めた動作をテストする
 * - 「ユーザーがブラウザで操作したとき」を自動で再現する
 *
 * たとえ話：実際に車に乗って「走る・曲がる・止まる」を全部テストする感じ
 *
 * 例：「支出登録画面にデータを送ったとき、DBに保存されるか？」
 *
 * 【Day10で主に使うのは Feature Test です】
 * expenses（支出管理）やtasks（タスク管理）の
 * 「登録・表示・編集・削除」が正しく動くかを確認します。
 */

/**
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * ✅ 【2. テスト用DBの準備（SQLite設定）】
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 *
 * 【なぜテスト用DBが必要か？】
 *
 * テストは「本番データを使わない」のが鉄則です。
 * テストで登録・削除を繰り返すと、本番DBのデータが壊れてしまいます。
 *
 * たとえ話：料理の練習は本番のお客様の食材ではなく、練習用の食材でやる
 *
 * 【SQLite（エスキューライト）とは？】
 *
 * SQLite = 超軽量のDB。ファイル1つ、またはメモリ上だけで動く。
 * テスト用途に最適で、テストが終わったらデータが消える「使い捨てDB」として使います。
 *
 * 【設定手順】
 *
 * 2つのファイルを変更します。
 *
 * ─────────────────────────────────────────
 * ① phpunit.xml のコメントアウトを外す
 * ─────────────────────────────────────────
 *
 * 変更前（24・25行目）：
 * <!-- <env name="DB_CONNECTION" value="sqlite"/> -->
 * <!-- <env name="DB_DATABASE" value=":memory:"/> -->
 *
 * 変更後：
 * <env name="DB_CONNECTION" value="sqlite"/>
 * <env name="DB_DATABASE" value=":memory:"/>
 *
 * 「:memory:」= メモリ上だけに作る、という意味。
 * テストが終わったら自動的に消えます。
 *
 * ─────────────────────────────────────────
 * ② SQLite のPHP拡張機能をDockerコンテナにインストールする
 * ─────────────────────────────────────────
 *
 * SQLiteを使うにはPHPにSQLite用の拡張機能が必要です。
 * 以下のコマンドをターミナルで実行してください：
 */

