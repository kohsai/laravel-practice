/**
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * ✅ 【3. テストファイルの構造】
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 *
 * テストファイルはすでに2つあります：
 * - tests/Feature/ExampleTest.php
 * - tests/Unit/ExampleTest.php
 *
 * 【Feature/ExampleTest.php の中身を見てみましょう】
 *
 * <?php
 * namespace Tests\Feature;
 * use Tests\TestCase;
 *
 * class ExampleTest extends TestCase
 * {
 *     public function test_the_application_returns_a_successful_response(): void
 *     {
 *         $response = $this->get('/');
 *         $response->assertStatus(200);
 *     }
 * }
 *
 * 【分解して読む】
 *
 * class ExampleTest extends TestCase
 * → 「ExampleTestクラス」は「TestCase（テストの基本機能）」を継承している
 *
 * public function test_〇〇(): void
 * → メソッド名が「test_」で始まる = これがテストの1つ
 * → 「void（ボイド）」= 戻り値なし
 *
 * $response = $this->get('/')
 * → 「/」というURLにGETリクエストを送る（ブラウザでアクセスする動作を再現）
 * → 結果を $response（レスポンス）という箱に入れる
 *
 * $response->assertStatus(200)
 * → 「assert（アサート）」= 「〜であることを確認する」という意味
 * → assertStatus(200) = 「HTTPステータスコードが200（成功）であることを確認」
 *
 * 【よく使うassert（アサート）一覧】
 *
 * assertStatus(200)     = 画面が正常に表示されるか
 * assertStatus(302)     = リダイレクト（別ページへ転送）されるか
 * assertDatabaseHas()   = DBに指定のデータが存在するか
 * assertDatabaseMissing() = DBに指定のデータが存在しないか
 * assertSee('文字列')   = 画面に指定の文字が表示されているか
 * assertRedirect('/url') = 指定のURLにリダイレクトされるか
 */

