/**
 * → 3つのタグ（食費・娯楽・節約）が表示されれば成功
 *
 * 【fresh()（フレッシュ）とは？】
 * 読み方：フレッシュ
 * 意味：「データベースから最新の情報を取り直す」
 *
 * たとえ話：
 * キャッシュ（一時保存）されている古いデータではなく、
 * 図書館（データベース）に直接行って最新の状態を確認する
 *
 * ━━━━━━━━━━━━━━━━━━━━
 * 【5-2. detach()（デタッチ）- 関係を削除する】
 * ━━━━━━━━━━━━━━━━━━━━
 *
 * 意味：「このユーザーからこのタグを削除する（中間テーブルの行を削除）」
 *
 * たとえ話：
 * 田中くんがサッカー部を「退部」する
 * → 名簿（中間テーブル）から「田中くん × サッカー部」の行が削除される
 */

// 【tinkerで実行】以下のコードをコピーして実行してください：
$user->tags()->detach(2);

/**
 * → タグid:2（娯楽）が削除されます
 *
 * 【確認】
 */

// 【tinkerで実行】以下のコードをコピーして実行してください：
$user->fresh()->tags;

/**
 * → 「食費」「節約」の2つだけ表示されれば成功（娯楽が消えている）
 *
 * 【全部のタグを削除する場合】
 */

// ※ 実際には実行しなくていいです。知識として確認してください：
