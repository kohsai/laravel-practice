 *
 * 【Day5のおさらい：1対多リレーション】
 *
 * Day5で「1対多（いちたいた）」リレーションを学びました。
 *
 * 例：ユーザーと支出（Expense）の関係
 * - ユーザー1人 → 支出は複数持てる
 * - 支出1件 → ユーザーは1人だけ
 *
 * 「1対多」＝「1人が複数を持つ、でも逆は1つだけ」という関係
 *
 * 【多対多（たたいた）とは？】
 *
 * 読み方：たたいた
 * 英語：many-to-many（メニートゥーメニー）
 *
 * 「多対多」＝「両方向で複数持てる」という関係
 *
 * 例：ユーザーとタグの関係
 * - ユーザー1人 → タグを複数つけられる（「節約」「食費」「趣味」）
 * - タグ1個 → 複数のユーザーに使われている
 *
 * 例：学生と授業の関係
 * - 学生1人 → 複数の授業を受けられる
 * - 授業1つ → 複数の学生が受けられる
 *
 * 例：映画と俳優の関係
 * - 映画1本 → 複数の俳優が出演している
 * - 俳優1人 → 複数の映画に出演している
 *
 * 【たとえ話：給食の当番表で考える】
 *
 * 1対多の場合（先生と生徒）：
 * 「田中先生のクラス」→ 田中先生は1人、生徒は30人
 * 生徒は1つのクラスにしか所属できない
 *
 * 多対多の場合（部活）：
 * 「サッカー部」「テニス部」「美術部」という部活がある
 * - 田中くん → サッカー部、美術部に入っている
 * - 鈴木さん → テニス部に入っている
 * - 佐藤くん → サッカー部、テニス部、美術部に入っている
 *
 * ポイント：
 * - 1人の生徒が複数の部活に入れる
 * - 1つの部活に複数の生徒が入れる
 * これが「多対多」
 *
 * 【今日学ぶこと】
 * 1. 中間テーブル（ちゅうかんてーぶる）の仕組み
 * 2. 多対多リレーションの設定方法
 * 3. attach（アタッチ）・detach（デタッチ）・sync（シンク）の操作
 */

/**
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * ✅ 【1. 中間テーブルとは？】
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 *
 * 中間テーブル（ちゅうかんてーぶる）＝「多対多の関係をつなぐ専用の表」
 * 英語：pivot table（ピボットテーブル）またはintermediate table（インターミディエイトテーブル）
 *
 * 【なぜ中間テーブルが必要か？】
 *
 * データベースは「多対多」を直接表現できません。
 * そのため、間に「つなぎ役」の表が必要です。
 *
 * たとえ話：部活の名簿管理
 *
 * ❌ 直接つなごうとすると問題が起きる：
 *
 * 生徒テーブル：
 * | id | name   | 部活1    | 部活2    | 部活3    |
 * |----|--------|---------|---------|---------|
 * | 1  | 田中くん| サッカー部| 美術部   | （空）   |
 * | 2  | 鈴木さん| テニス部 | （空）   | （空）   |
 *
 * 問題：部活が4つになったら？列が足りない…
 *
 * ✅ 中間テーブルを使えば解決：
 *
 * 生徒テーブル（usersの代わりに）：
 * | id | name    |
 * |----|---------|
 * | 1  | 田中くん |
 * | 2  | 鈴木さん |
 * | 3  | 佐藤くん |
 *
 * 部活テーブル（tagsの代わりに）：
 * | id | name    |
 * |----|---------|
 * | 1  | サッカー部|
 * | 2  | テニス部  |
 * | 3  | 美術部   |
 *
 * 中間テーブル（student_club）：← これがつなぎ役
 * | student_id | club_id |
 * |------------|---------|
 * | 1          | 1       |  ← 田中くん × サッカー部
 * | 1          | 3       |  ← 田中くん × 美術部
 * | 2          | 2       |  ← 鈴木さん × テニス部
 * | 3          | 1       |  ← 佐藤くん × サッカー部
 * | 3          | 2       |  ← 佐藤くん × テニス部
 * | 3          | 3       |  ← 佐藤くん × 美術部
 *
 * これで「何部でも入れる」「1つの部活に何人でも入れる」が実現できる！
 *
 * 【Laravelでの実践テーマ】
 *
 * 今回は「ユーザーとタグ」の多対多を実装します。
 *
 * - users テーブル（ユーザー）：既存
 * - tags テーブル（タグ）：新規作成
 * - tag_user テーブル（中間テーブル）：新規作成
 *
 * ※ Laravel慣習：中間テーブルは「モデル名_モデル名」をアルファベット順で並べる
 *   tag + user → tag_user（t < u なのでtagが先）
 */

