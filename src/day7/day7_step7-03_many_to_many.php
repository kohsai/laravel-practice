/**
 * 作成された2つのマイグレーションファイルを以下の内容で編集してください。
 *
 * ━━━━━━━━━━━━━━━━━━━━
 * ① tags テーブルのマイグレーション
 * ファイル：database/migrations/xxxx_create_tags_table.php
 * ━━━━━━━━━━━━━━━━━━━━
