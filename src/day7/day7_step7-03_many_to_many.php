/**
 * ━━━━━━━━━━━━━━━━━━━━
 * 【5-3. sync()（シンク）- 指定したものだけに揃える】
 * ━━━━━━━━━━━━━━━━━━━━
 *
 * 意味：「指定したタグだけになるように、追加・削除を自動でやってくれる」
 *
 * たとえ話：
 * 「私の部活はテニス部と美術部だけにしてください」と申請する
 * → 今サッカー部に入っていれば自動で退部
 * → テニス部・美術部に入っていなければ自動で入部
 * → 「今の状態」ではなく「最終的にこうしてほしい」を指定する
 *
 * 現在の状態：食費(id:1)、節約(id:3) を持っている
 */

// 【tinkerで実行】以下のコードをコピーして実行してください：
$user->tags()->sync([1, 2]);

/**
 * → タグが「食費(id:1)」「娯楽(id:2)」だけになります
 *   節約(id:3) は自動で削除、娯楽(id:2) は自動で追加
 *
 * 【確認】
 */

// 【tinkerで実行】以下のコードをコピーして実行してください：
$user->fresh()->tags;

/**
 * → 「食費」「娯楽」の2つだけ表示されれば成功
 *
 * 【attach・detach・syncの使い分けまとめ】
 *
 * attach()：「これを追加したい」→ 1件または複数件を追加
 *   例：タグを1つ選んで「追加」ボタンを押す
 *
 * detach()：「これを削除したい」→ 1件または複数件を削除
 *   例：タグを1つ選んで「削除」ボタンを押す
 *
 * sync()：「最終的にこれだけにしてほしい」→ 全部を一括で入れ替え
 *   例：チェックボックスで複数タグを選んで「保存」ボタンを押す
 *       → 選んだものだけが残り、選んでいないものは自動削除
