/**
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * ✅ 【7. Q&A（よくある疑問）】
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 *
 * Q1. belongsTo と belongsToMany の違いは？
 *
 * A1. 「どちら側が複数を持てるか」の違いです。
 *
 *     belongsTo（1対多の「多」側）：
 *     「私は1つのモデルに属している」
 *     例：Expense（支出）は1人のUser（ユーザー）に属している
 *     → Expenseモデルに書く
 *
 *     belongsToMany（多対多）：
 *     「私は複数のモデルに属している」
 *     例：Userは複数のTagに属している、Tagも複数のUserに属している
 *     → 両方のモデルに書く
 *
 * ━━━━━━━━━━━━━━━━━━━━
 *
 * Q2. なぜ中間テーブルは timestamps() が任意なの？
 *
 * A2. timestamps() を付けると「作成日時・更新日時」が記録されます。
 *     中間テーブルはシンプルな「つなぎ役」なので、
 *     「いつつながったか」が不要な場合は省略できます。
 *
 *     ただし今回は付けています（Laravelのデフォルト動作と合わせるため）。
 *     もし timestamps() を外す場合は、モデルで $timestamps = false も設定します。
 *
 * ━━━━━━━━━━━━━━━━━━━━
 *
 * Q3. sync() は追加しかしないの？削除もする？
 *
 * A3. sync() は追加も削除も両方します。
 *
 *     例：今「食費・節約」を持っている状態で sync([1, 2]) を実行すると…
 *     - タグid:1（食費）：既にあるのでそのまま（何もしない）
 *     - タグid:2（娯楽）：ないので追加
 *     - タグid:3（節約）：指定されていないので削除
 *
 *     「最終的にこうなってほしい」という状態を指定すれば、
 *     必要な追加・削除を自動でやってくれます。
 *
 * ━━━━━━━━━━━━━━━━━━━━
 *
 * Q4. attach() を2回同じIDで実行したらどうなる？
 *
 * A4. 中間テーブルに重複した行が入ってしまいます。
 *
 *     例：
 *     $user->tags()->attach(1); // 1回目
 *     $user->tags()->attach(1); // 2回目 → 同じ行がもう1つ追加される！
 *
 *     これを防ぎたい場合は syncWithoutDetaching() を使います：
 *     $user->tags()->syncWithoutDetaching([1]);
 *     → 既にあれば追加しない、なければ追加する（削除はしない）
 *
 * ━━━━━━━━━━━━━━━━━━━━
 *
 * Q5. 中間テーブルに追加の列を持たせることはできる？
 *
 * A5. できます。例えば「このタグをつけた日時」や「メモ」を中間テーブルに持たせることができます。
 *
 *     その場合はマイグレーションに列を追加して、
 *     モデルのリレーション定義に ->withPivot('列名') を追加します：
 *
 *     return $this->belongsToMany(Tag::class)->withPivot('memo');
 *
 *     ただし今回は基本形を学んでいるので、追加列は使いません。
 */

/**
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 📚 【用語集】
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 *
 * 多対多（たたいた）
 * └─ many-to-many（メニートゥーメニー）。両方向で複数の関係を持てる
 *
 * 中間テーブル（ちゅうかんてーぶる）
 * └─ 多対多の関係をつなぐ専用の表。pivot table（ピボットテーブル）ともいう
 *
 * belongsToMany（ビロングストゥーメニー）
 * └─ 「複数のモデルに属する」多対多リレーションの設定メソッド
 *
 * attach（アタッチ）
 * └─ 中間テーブルに行を追加する（関係を結ぶ）
 *
 * detach（デタッチ）
 * └─ 中間テーブルから行を削除する（関係を切る）
 *
 * sync（シンク）
 * └─ 指定したIDだけになるよう、追加・削除を自動で行う
 *
 * fresh()（フレッシュ）
 * └─ データベースから最新情報を取り直す
 *
 * foreignId（フォーリンアイディ）
 * └─ 外部キーの列を作るメソッド。他テーブルのidを参照する
 *
 * constrained（コンストレインド）
 * └─ 外部キー制約を設定する。参照先のテーブルを自動推測
 *
 * cascade（カスケード）
 * └─ 参照先が削除されたとき、一緒に削除する設定
 */

