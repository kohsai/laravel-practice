/**
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * ✅ 【5. attach()・detach()・sync()の操作】
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 *
 * 多対多リレーションには3つの専用メソッドがあります。
 *
 * | メソッド  | 読み方  | 意味                 | たとえ話                |
 * |----------|---------|---------------------|------------------------|
 * | attach() | アタッチ | 関係を追加する        | 部活に入部する          |
 * | detach() | デタッチ | 関係を削除する        | 部活を退部する          |
 * | sync()   | シンク  | 指定したものだけに揃える| 部活を全部一括変更する  |
 *
 * ━━━━━━━━━━━━━━━━━━━━
 * 【5-1. attach()（アタッチ）- 関係を追加する】
 * ━━━━━━━━━━━━━━━━━━━━
 *
 * 意味：「このユーザーにこのタグを追加する（中間テーブルに行を追加）」
 *
 * たとえ話：
 * 田中くんがサッカー部に「入部申請」を出す
 * → 名簿（中間テーブル）に「田中くん × サッカー部」という行が追加される
 */

