// 【xxxx_create_tags_table.php の up() メソッドの中身】以下をコピーして貼り付けてください：
Schema::create('tags', function (Blueprint $table) {
    $table->id();
    $table->string('name');       // タグ名（例：「食費」「娯楽」）
    $table->timestamps();
});

/**
 * ━━━━━━━━━━━━━━━━━━━━
 * ② tag_user テーブルのマイグレーション
 * ファイル：database/migrations/xxxx_create_tag_user_table.php
 * ━━━━━━━━━━━━━━━━━━━━
 *
 * 【重要ポイント】
 *
 * 中間テーブルには以下の特徴があります：
 * - $table->id() は必須ではない（省略可能）
 * - $table->timestamps() は任意
 * - 外部キー（がいぶきー）を2つ設定する
 *
 * 外部キー（がいぶきー）＝「他のテーブルの id を参照する列」
 *
 * たとえ話：
 * 中間テーブルの「tag_id = 1」とは「tagsテーブルのid=1のタグ」を指している
 * 他のテーブルへの「参照（住所録）」のようなもの
 */

// 【xxxx_create_tag_user_table.php の up() メソッドの中身】以下をコピーして貼り付けてください：
Schema::create('tag_user', function (Blueprint $table) {
    $table->id();
    $table->foreignId('tag_id')->constrained()->onDelete('cascade');
    $table->foreignId('user_id')->constrained()->onDelete('cascade');
    $table->timestamps();
});

/**
 * 【コードの意味】
 *
 * foreignId('tag_id')（フォーリンアイディ）
 *   = 「tag_idという列を作る。これは外部キー（よそのテーブルのidを参照する列）だよ」
 *
 * ->constrained()（コンストレインド）
 *   = 「tagsテーブルのidを参照するよ」（自動的にテーブル名を推測）
 *
 * ->onDelete('cascade')（オンデリートカスケード）
 *   = 「参照先のタグが削除されたら、この行も一緒に削除する」
 *
 * たとえ話（cascade）：
 * タグ「食費」が削除されたら、「このユーザーは食費タグを持っている」という
 * 中間テーブルの行も自動的に消してくれる（孤立したデータが残らない）
 *
 * 【マイグレーション実行】
 */

// 【ターミナルで実行】以下のコマンドをコピーして実行してください：
