 * → resources/views/components/card.blade.php が作成されます
 *
 * 【手順5：card.blade.php の内容を書く】
 *
 * 以下の内容に書き換えてください：
 *
 * <div style="border: 1px solid #ccc; padding: 16px; margin-bottom: 16px; border-radius: 4px;">
 *     {{ $slot }}
 * </div>
 *
 * 【手順6：index.blade.phpの表をcardで囲む】
 *
 * <x-card>と</x-card>の間に表全体を入れます：
 *
 * <x-card>
 *     <table border="1" cellpadding="8" cellspacing="0">
 *         ...（表の中身）...
 *     </table>
 * </x-card>
 *
 * <x-card>と</x-card>の間に書いた表全体（<table>から</table>まで）が
 * まるごと$slotになり、card.blade.phpの{{ $slot }}の場所に表示されます。
 * ブラウザで見える「枠で囲まれた表」は、card.blade.phpの枠の中に
 * 表がそっくり差し込まれた結果です。
