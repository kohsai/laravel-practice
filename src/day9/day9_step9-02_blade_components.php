/**
 * 【alert.blade.php の内容】
 *
 * @props(['type' => 'success', 'message' => null])
 *
 * @if ($message)
 *     <div style="
 *         border: 1px solid {{ $type === 'success' ? 'green' : 'red' }};
 *         color: {{ $type === 'success' ? 'green' : 'red' }};
 *         padding: 10px;
 *         margin: 10px 0;
 *     ">
 *         {{ $message }}
 *     </div>
 * @endif
 *
 * 【@props（アットプロップス）とは？】
 *
 * 注文票のようなものです。
 * お店に「アラート部品」を注文するとき、注文票に記入欄があります。
 *
 * - type欄：「記入がなければ自動的に'success'と印字される」
 * - message欄：「記入がなければ空白のまま」
 *
 * つまり「渡さなかった場合の初期値があらかじめ決まっている」というイメージです。
 * 渡せば上書きされ、渡さなければ決められた初期値が使われます。
 *
 * @if ($message)：
 * messageが存在する（空でない）場合だけ表示します。
 *
 * $type === 'success' ? 'green' : 'red'：
 * typeが 'success' なら緑色、それ以外は赤色にします。
 * （三項演算子（さんこうえんざんし）といいます）
 */

/**
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * ✅ 【3. alertコンポーネントを使ってみよう】
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 *
 * 【手順3：index.blade.php の既存メッセージを置き換える】
 *
 * 現在の index.blade.php にある以下の部分：
 *
 * @if (session('success'))
 *     <div style="color: green; border: 1px solid green; padding: 10px; margin: 10px 0;">
 *         {{ session('success') }}
 *     </div>
 * @endif
 *
 * を、以下の1行に置き換えてください：
 *
 * <x-alert type="success" :message="session('success')" />
 *
 * 【:(コロン)あり・なしの違い】
 *
 * レシピカードで例えます。
 *
 * コロンなし → 「卵2個」という文字をそのまま渡す（文字列）
 *   type="success" = 「success」という文字をそのまま渡す
 *
 * コロンあり → 冷蔵庫を開けて実際に確認した結果を渡す（PHP実行）
 *   :message="session('success')" = セッションを実際に調べた結果を渡す
 *
 * session('success') はPHPの関数（冷蔵庫を開けて調べる作業）なので、
 * コロンが必要です。コロンなしで書くと「session('success')」という
 * 文字列がそのまま渡されてしまい、関数が実行されません。
 *
 * 【処理の流れ】
 *
 * ① フォームで支出を登録
 *    ↓
 * ② ExpenseControllerのstore()が動く
 *    ↓
 * ③ store()の最後でセッションに「支出を登録しました」を一時保存してリダイレクト
 *    return redirect()->route('expenses.index')->with('success', '支出を登録しました');
 *    ↓
 * ④ index.blade.phpが表示される際にsession('success')でセッションから文字列を取り出す
 *    ↓
 * ⑤ <x-alert :message="session('success')" />でalertコンポーネントに渡す
 *    ↓
 * ⑥ alert.blade.phpの{{ $message }}に「支出を登録しました」が表示される
 *
 * ※「支出を登録しました」という文字列はindex.blade.phpの中にはありません。
 *   ExpenseControllerで書かれた文字列が、セッション経由でalertに届いています。
 */

/**
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * ✅ 【4. cardコンポーネントを作ろう（slot を使う）】
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 *
 * 次に「カード（枠で囲む部品）」のコンポーネントを作ります。
 * ここでは「slot（スロット）」という仕組みを学びます。
 *
 * 【alertとcardの違い】
 *
 * - alert.blade.php = 値を受け取って表示する部品
 *   messageという値を外から渡してもらい、それを表示します。
 *   アクセサリーで例えると「メッセージを刻印済みのプレート」です。
 *
 * - card.blade.php = 中に何かを包む部品
 *   何を入れるかは使う側が自由に決めます。
 *   アクセサリーで例えると「中に何でも入れられる空の額縁」です。
 *
 * 【$slot（スロット）とは？】
 *
 * コンポーネントの中の「何でも入れていい空き場所」です。
 * ガチャポンのカプセルで例えます。
 * カプセル（card）の外側は決まっていますが、中身（$slot）は何を入れても自由です。
 *
 * 【手順4：cardコンポーネントを作る】
 */

// ターミナルで以下のコマンドを実行してください：
// （docker compose exec php php artisan make:component Card --view）

