/**
 * 【alert.blade.php の内容】
 *
 * @props(['type' => 'success', 'message' => null])
 *
 * @if ($message)
 *     <div style="
 *         border: 1px solid {{ $type === 'success' ? 'green' : 'red' }};
 *         color: {{ $type === 'success' ? 'green' : 'red' }};
 *         padding: 10px;
 *         margin: 10px 0;
 *     ">
 *         {{ $message }}
 *     </div>
 * @endif
 *
 * 【コードの読み方】
 *
 * @props（アットプロップス）：
 * このコンポーネントが「受け取る値（プロパティ）」を定義します。
 * 材料リストのようなものです。
 *   'type' => 'success' → typeという値を受け取る。省略時は 'success'
 *   'message' => null   → messageという値を受け取る。省略時はnull（空）
 *
 * @if ($message)：
 * messageが存在する（空でない）場合だけ表示します。
 *
 * $type === 'success' ? 'green' : 'red'：
 * typeが 'success' なら緑色、それ以外は赤色にします。
 * （三項演算子（さんこうえんざんし）といいます）
 */

