/**
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * ✅ 【6. なぜファイルを分けるのか】
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 *
 * 役割分担が多くのファイルに細分化されているのは、
 * 慣れるまでは混乱することもあります。
 * ただ、慣れてくると逆に「全部1ファイルに書いてある方が混乱する」
 * と感じるようになります。
 *
 * 【ファイルを分ける理由】
 *
 * ① エラーの発見がしやすい
 *   表示がおかしければBladeを見る、保存がおかしければControllerを見る、
 *   と調べる場所が絞れます。
 *
 * ② 修正が1カ所で済む
 *   alertのデザインを変えたければalert.blade.phpだけ直せばよく、
 *   使っている全ページに自動で反映されます。
 *
 * ③ 読みやすい
 *   1つのファイルに全部書くと何百行にもなります。
 *   役割ごとに分けることで、各ファイルが短くシンプルに保てます。
 *
 * 【混乱しにくくなるコツ】
 *
 * ① 「入口から出口」で追う
 *   ユーザーがボタンを押す → ルート → Controller → Model → View
 *   という一方通行の流れが基本です。迷ったら入口（ルート）から追ってください。
 *
 * ② 「どのファイルが何の担当か」を覚える
 *   見た目がおかしい → Bladeを見る
 *   データがおかしい → ControllerかModelを見る
 *   URLがおかしい   → ルートを見る
 *
 * これがLaravelがMVC（エムブイシー）という考え方を採用している理由です。
 * 「Model・View・Controller」それぞれに役割を分担させて、管理しやすくしています。
 */

/**
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * ✅ 【7. よくある質問（Q&A）】
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 *
 * Q1. @extends と コンポーネントの違いは何ですか？
 * A1. 役割が違います。
 *     @extends（レイアウト）= ページ全体の「骨格」を共通化する
 *     コンポーネント（x-〇〇）= ページの中の「部品」を再利用する
 *     たとえ話：@extends = 建物の「間取り」、コンポーネント = 建物の「家具」
 *
 * Q2. なぜファイル名は小文字で書くの？
 * A2. Laravelのルールです。
 *     コンポーネントのファイルは必ず小文字で保存します。
 *     Alert.blade.php ではなく alert.blade.php です。
 *     呼び出しは <x-alert /> と小文字で書きます。
 *
 * Q3. @props がないコンポーネントでも動くの？
 * A3. 動きます。ただ書いた方がデフォルト値を設定できて便利です。
 *
 * Q4. $slot は必ず使わないといけないの？
 * A4. 必要なコンポーネントにだけ使います。
 *     alert のように「値だけ受け取ればいい場合」は $slot は不要です。
 *     card のように「中に任意の内容を入れたい場合」は $slot を使います。
 *
 * Q5. コンポーネントのファイルを間違えて作ってしまったら？
 * A5. resources/views/components/ フォルダのファイルをVSCodeから直接削除して
 *     作り直せます。artisan コマンドで削除はできません。
 *
 * Q6. make:component に --view を付けないと何が作られる？
 * A6. ファイルが2つ作られます。
 *
 *     ・resources/views/components/alert.blade.php（表示担当・今回作ったもの）
 *     ・app/View/Components/Alert.php（処理担当・今回は作らなかったもの）
 *
 *     Alert.php（処理担当）は「typeがsuccessなら'green'を返す」という
 *     判定作業をするファイルです。
 *     料理で例えると「食材を見て新鮮か古いかを判定するスタッフ」です。
 *
 *     alert.blade.php（表示担当）はAlert.phpが判定した結果を受け取って
 *     画面に表示するだけです。「判定結果を受け取ってお皿に盛り付けるスタッフ」です。
 *
 *     今の段階では処理が単純なのでAlert.phpは不要です。
 *     GodeVenのMVP開発段階でも --view だけで十分対応できます。
 */

/**
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 📖 【用語集】
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 *
 * コンポーネント（Component）
 * └─ ページの中で繰り返し使う「部品」のこと
 *
 * x-〇〇（エックスハイフン〇〇）
 * └─ Bladeコンポーネントを呼び出す記法。<x-alert /> のように使う
 *
 * @props（アットプロップス）
 * └─ コンポーネントが受け取る値を定義する「注文票」のようなもの
 *    デフォルト値（省略した時の初期値）も設定できる
 *
 * $slot（スロット）
 * └─ コンポーネントの中の「何でも入れていい空き場所」
 *    <x-card>〜</x-card>の間に書いたものがここに差し込まれる
 *
 * :(コロン付き属性)
 * └─ 属性値をPHPとして実行する記法
 *    コロンなし→文字列そのまま、コロンあり→PHPを実行した結果を渡す
 *
 * make:component（メイク コンポーネント）
 * └─ コンポーネントファイルを自動生成するartisanコマンド
 *
 * --view（ビューオプション）
 * └─ make:component に付けるオプション。Bladeファイルのみ生成する
 *
 * view:clear（ビュー クリア）
 * └─ Bladeのキャッシュを削除するartisanコマンド
 *
 * MVC（エムブイシー）
 * └─ Model・View・Controllerの略。役割を3つに分けて管理しやすくする考え方
 */

/**
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 🔗 【Step9-03 への接続】
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 *
 * Step9-02で「コンポーネント（部品）」を作る方法を学びました。
 * Step9-03では、expenses系の3ファイル（index・create・edit）を
 * Step9-01で作ったレイアウト（@extends）に統合します。
 * その際、今回作ったalertコンポーネントも活用します。
 */
