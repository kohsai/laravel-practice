/**
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * ✅ 【6. よくある質問（Q&A）】
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 *
 * Q1. @extends と コンポーネントの違いは何ですか？
 * A1. 役割が違います。
 *
 *     @extends（レイアウト）= ページ全体の「骨格」を共通化する
 *     → 例：ヘッダー・フッターを全ページで統一する
 *
 *     コンポーネント（x-〇〇）= ページの中の「部品」を再利用する
 *     → 例：ボタン・アラートを複数ページで使い回す
 *
 *     たとえ話：
 *     @extends = 建物の「間取り」（全体構造）
 *     コンポーネント = 建物の「家具」（部品）
 *
 * Q2. なぜファイル名は小文字で書くの？
 * A2. Laravelのルールです。
 *     コンポーネントのファイルは必ず小文字・スネークケースで保存します。
 *     Alert.blade.php ではなく alert.blade.php です。
 *     呼び出しは <x-alert /> と小文字で書きます。
 *
 * Q3. @props がないコンポーネントでも動くの？
 * A3. 動きます。
 *     @props は「受け取る値を明示する」ための宣言です。
 *     書かなくても値を受け取れますが、書いた方がコードが分かりやすくなります。
 *     また、デフォルト値（省略した時の値）を設定できるので書くことを推奨します。
 *
 * Q4. $slot は必ず使わないといけないの？
 * A4. 必要なコンポーネントにだけ使います。
 *     alert コンポーネントのように「値だけ受け取ればいい場合」は $slot は不要です。
 *     card コンポーネントのように「中に任意の内容を入れたい場合」は $slot を使います。
 *
 * Q5. コンポーネントのファイルを間違えて作ってしまったら？
 * A5. resources/views/components/ フォルダの中のファイルを
 *     そのまま削除して作り直せます。
 *     artisan コマンドで削除はできないので、VSCodeから直接削除してください。
 *
 * Q6. make:component に --view を付けないと何が作られる？
 * A6. PHPクラスファイル（app/View/Components/Alert.php）と
 *     Bladeファイルの両方が作られます。
 *     クラスファイルを使うとPHPのロジック（処理）をコンポーネントに持たせられますが、
 *     今の段階では --view だけで十分です。
 */

/**
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 📖 【用語集】
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 *
 * コンポーネント（Component）
 * └─ ページの中で繰り返し使う「部品」のこと
 *
 * x-〇〇（エックスマイナス〇〇）
 * └─ Bladeコンポーネントを呼び出す記法。<x-alert /> のように使う
 *
 * @props（アットプロップス）
 * └─ コンポーネントが受け取る値（プロパティ）を定義する宣言
 *
 * $slot（スロット）
 * └─ コンポーネントの中に「内容を差し込む場所」を作る仕組み
 *
 * :(コロン付き属性)
 * └─ 属性値をPHPとして評価する記法。:message="session('success')" のように使う
 *
 * make:component（メイク コンポーネント）
 * └─ コンポーネントファイルを自動生成するartisanコマンド
 *
 * --view（ビューオプション）
 * └─ make:component に付けるオプション。Bladeファイルのみ生成する（PHPクラスなし）
 *
 * view:clear（ビュー クリア）
 * └─ Bladeのキャッシュを削除するartisanコマンド。コンポーネントが見つからないエラー時に使う
 */

