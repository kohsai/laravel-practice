 * ✅ 【3. コンポーネントを使ってみよう】
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 *
 * 作ったコンポーネントを実際に使います。
 *
 * 【手順3：index.blade.php の既存メッセージを置き換える】
 *
 * 現在の index.blade.php にある以下の部分：
 *
 * @if (session('success'))
 *     <div style="color: green; border: 1px solid green; padding: 10px; margin: 10px 0;">
 *         {{ session('success') }}
 *     </div>
 * @endif
 *
 * を、以下の1行に置き換えてください：
 *
 * <x-alert type="success" :message="session('success')" />
 *
 * 【ポイント：:(コロン)の意味】
 * :message="session('success')"  ← コロンあり
 * message="固定の文字列"         ← コロンなし
 *
 * コロンがある場合：「""の中をPHPとして実行する」
 * コロンがない場合：「""の中をそのまま文字列として渡す」
 *
 * session('success') はPHPの関数なので、コロンが必要です。
 */

