/**
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 💡 【補足：なぜ nginx の設定も必要なのか？】
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 *
 * 【2つの「サイズ制限」が存在する】
 *
 * ファイルアップロードには、2つの異なる制限があります：
 *
 * ① nginx（エンジンエックス）の受信制限
 *    場所：docker/nginx/default.conf
 *    設定：client_max_body_size 20M
 *    意味：「Laravelにファイルを渡す前の段階」でのサイズ制限
 *    これを超えると「413 Request Entity Too Large」エラーになる
 *    → Step8-02で 20M に設定済みです
 *
 * ② Laravelのバリデーション制限
 *    場所：StoreUserRequest.php
 *    設定：max:2048
 *    意味：「Laravelが受け取った後」のサイズチェック
 *    これを超えると日本語のエラーメッセージが表示される
 *    → 今回追加します
 *
 * 【流れのイメージ】
 *
 * ユーザーがファイルを送信
 *     ↓
 * nginx が受け取る（ここで 20MB 以上は弾く）
 *     ↓
 * Laravel が受け取る（ここで 2MB 以上は弾く＋エラーメッセージ表示）
 *     ↓
 * 保存処理
 *
 * 【たとえ話：マンションの宅配ボックス（続き）】
 * - nginx の制限 = マンションのエントランスで断る（「大きすぎて入れません」）
 * - Laravel の制限 = 部屋の前で断る（「規定サイズ超えています」）
 *
 * 今回は「2MBまで」を Laravel のバリデーションで実装するので、
 * nginx は 20MB まで許可しておくことで問題ありません。
 */

/**
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * ❓ 【よくある質問 Q&A】
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 *
 * Q1. image ルールと mimes ルール、両方必要ですか？
 * A1. どちらか一方でも動きますが、両方書く理由があります。
 *
 *     image のみ：jpeg/png/bmp/gif/svg/webp を許可（SVGも通る）
 *     mimes のみ：指定した形式のみ（ただし image チェックより緩い）
 *     両方：「画像であること」＋「許可形式のみ」の二重チェック
 *
 *     実務ではどちらか一方が多いですが、
 *     今回は学習目的で両方書いています。
 *     mimes:jpeg,png,gif だけでも十分機能します。
 *
 * Q2. max:2048 の単位は何ですか？
 * A2. キロバイト（KB）です。
 *     2048KB = 2MB です。
 *     Laravelのmax ルールはKBを単位として使います。
 *
 * Q3. 編集フォーム（update）でも同じバリデーションが適用されますか？
 * A3. はい。StoreUserRequest は store() と update() の両方で使われています。
 *     なので1か所変更するだけで両方に適用されます。
 *     編集時も「画像を差し替えるなら 2MB 以下・jpeg/png/gif のみ」が守られます。
 *
 * Q4. gif を許可しているのはなぜですか？
 * A4. gif はアニメーション画像（動くファイル）として使われることもあります。
 *     許可するかはサービスの用途次第です。
 *     今回のレシート管理では gif の使用頻度は低いですが、
 *     許可しておいても問題はありません。
 *
 * Q5. webp（ウェブピー）は許可しなくて良いですか？
 * A5. 今回は jpeg/png/gif に絞っています。
 *     webp は Google が開発した次世代画像形式で、
 *     特にスマートフォンのカメラで撮影した画像が webp 形式のことがあります。
 *     必要に応じて mimes:jpeg,png,gif,webp と追加できます。
 */

/**
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 📚 【用語集】
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 *
 * バリデーション（Validation）
 * └─ 「検証」の意味。送られてきたデータが正しい形式か確認する処理
 *
 * nullable（ナラブル）
 * └─ 「nullでもOK」＝送られてこなくても許可する
 *
 * image（イメージ）ルール
 * └─ アップロードされたファイルが画像形式かチェックするバリデーション
 *
 * mimes（マイムズ）ルール
 * └─ 許可するファイル形式を指定するバリデーション
 *    mimes = MIME Type（マイムタイプ）の略
 *    MIME Type = ファイルの種類を表す識別子（例：image/jpeg、text/html）
 *
 * max（マックス）ルール
 * └─ 最大サイズを制限するバリデーション。ファイルの場合はKB単位
 *
 * KB（キロバイト）
 * └─ ファイルサイズの単位。1024KB = 1MB
 *
 * nginx（エンジンエックス）
 * └─ Webサーバーソフトウェア。Laravelよりも手前でリクエストを受け取る
 *    Docker環境では docker/nginx/default.conf で設定する
 *
 * client_max_body_size（クライアント・マックス・ボディ・サイズ）
 * └─ nginxが受け取れる最大リクエストサイズ。Step8-02で 20M に設定済み
 */
