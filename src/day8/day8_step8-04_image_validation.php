/**
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * ✅ 【1. 画像バリデーションのルール一覧】
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 *
 * 【Laravelで使える画像バリデーションルール】
 *
 * ─────────────────────────────────────────
 * 1. nullable（ナラブル）
 * ─────────────────────────────────────────
 *   意味：「送られてこなくてもOK」（画像は任意）
 *   使い道：画像は必須ではない場合
 *   例：'image' => 'nullable|image|...'
 *
 * ─────────────────────────────────────────
 * 2. image（イメージ）
 * ─────────────────────────────────────────
 *   意味：「画像ファイルであること」
 *   許可される形式：jpeg, png, bmp, gif, svg, webp
 *   使い道：画像以外のファイルを弾く
 *   例：'image' => 'nullable|image|...'
 *
 * ─────────────────────────────────────────
 * 3. mimes（マイムズ）
 * ─────────────────────────────────────────
 *   意味：「このファイル形式のみ許可」
 *   使い道：許可する形式を細かく指定したいとき
 *   例：'image' => 'nullable|mimes:jpeg,png,gif|...'
 *   ↑ この3種類だけ受け付ける
 *
 *   【image との違い】
 *   - image → svgやwebpも許可（広め）
 *   - mimes → 指定した形式だけ（細かく制御できる）
 *
 * ─────────────────────────────────────────
 * 4. max（マックス）
 * ─────────────────────────────────────────
 *   意味：「最大ファイルサイズ」
 *   単位：キロバイト（KB）
 *   例：max:2048 → 2048KB = 2MB まで
 *   例：max:5120 → 5120KB = 5MB まで
 *
 *   【KB換算早見表】
 *   500KB  → max:500
 *   1MB    → max:1024
 *   2MB    → max:2048  ← 今回使う
 *   5MB    → max:5120
 *   10MB   → max:10240
 *
 * ─────────────────────────────────────────
 * 5. dimensions（ダイメンションズ）
 * ─────────────────────────────────────────
 *   意味：「画像の縦横サイズを制限する」
 *   例：'image' => 'dimensions:min_width=100,min_height=100'
 *   使い道：アイコン画像で最低サイズを保証したいとき
 *   ※今回は使いません（参考として記載）
 */

