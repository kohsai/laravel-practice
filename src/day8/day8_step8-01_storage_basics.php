/**
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * ❓ 【よくある質問 Q&A】
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 *
 * Q1. storageリンクを作らないとどうなりますか？
 * A1. 画像ファイルはサーバーに保存されますが、ブラウザからURLでアクセスできません。
 *     画像を表示しようとすると「404 Not Found」になります。
 *
 * Q2. FILESYSTEM_DISK=local のままだとどうなりますか？
 * A2. Storage::put() で保存したファイルが storage/app/ に入ります。
 *     これは storage/app/public/ ではないため、storageリンクを通っても
 *     ブラウザからアクセスできません。
 *     必ず public に変更してください。
 *
 * Q3. Amazon S3（エスリー）って何ですか？
 * A3. Amazon が提供するクラウドのファイル置き場です。
 *     ファイルをインターネット上に保存してどこからでもアクセスできます。
 *     大規模なサービスで使われますが、今回は使いません。
 *
 * Q4. config:clear（コンフィグ・クリア）は何をしていますか？
 * A4. Laravelは .env の設定を「キャッシュ（かしゅ）」として保存しています。
 *     キャッシュ = 処理を速くするために保存しておく一時データ
 *     .env を変更してもキャッシュが古いままだと変更が反映されません。
 *     config:clear でキャッシュを削除することで、最新の .env が読み込まれます。
 *
 * Q5. storage/app/public/ とpublic/storage/ は何が違いますか？
 * A5. 本物と近道の違いです。
 *     storage/app/public/ = ファイルの本物の保存場所
 *     public/storage/     = storage/app/public/ への近道（ショートカット）
 *     ブラウザがアクセスするのは public/storage/ 経由ですが、
 *     実際のファイルは storage/app/public/ にあります。
 */

/**
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 📚 【用語集】
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 *
 * storage（ストレージ）
 * └─ ファイルを保存する場所。Laravelでは storage/ フォルダのこと。
 *
 * シンボリックリンク
 * └─ 別の場所にある本物のファイルへの「近道・ショートカット」
 *
 * FILESYSTEM_DISK（ファイルシステム・ディスク）
 * └─ ファイルをどこに保存するかを決める .env の設定項目
 *
 * storage:link（ストレージ・リンク）
 * └─ storageリンクを作成するartisanコマンド
 *
 * config:clear（コンフィグ・クリア）
 * └─ .envの設定キャッシュを削除するartisanコマンド
 *
 * キャッシュ
 * └─ 処理を速くするために一時的に保存しておくデータ。
 *    古くなると設定変更が反映されないため、定期的にクリアする。
 */

