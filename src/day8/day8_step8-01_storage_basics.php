/**
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 🧠 【今日学ぶこと】
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 *
 * Day8のテーマは「ファイルアップロードと画像処理」です。
 *
 * ユーザーが画像をWebアプリに送ってくる → それをサーバーに保存する
 * → 保存した画像をブラウザに表示する
 *
 * この一連の流れを学びます。
 *
 * 【Step8-01で学ぶこと】
 *
 * まず「ファイルをどこに保存するか」を理解します。
 * コードを書く前に、仕組みを頭に入れることが大切です。
 *
 * 今日学ぶ3つのこと：
 * 1. Laravelのフォルダ構成（storageフォルダとは何か）
 * 2. storageリンク（シンボリックリンク）の仕組み
 * 3. .envのファイルシステム設定
 */

/**
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * ✅ 【1. Laravelのフォルダ構成を理解する】
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 *
 * 【Laravelのファイル保存場所は2種類ある】
 *
 * ① public フォルダ（パブリック）
 *   └─ 誰でもブラウザからアクセスできる場所
 *   └─ CSS・JavaScript・画像（最初から入っているもの）を置く場所
 *   └─ 例：laravel-practice/public/
 *
 * ② storage フォルダ（ストレージ）
 *   └─ 直接ブラウザからアクセスできない「裏側の保管庫」
 *   └─ ユーザーがアップロードしたファイルを保存する場所
 *   └─ 例：laravel-practice/storage/app/public/
 *
 * 【たとえ話：お店の「店頭」と「倉庫」】
 *
 * public フォルダ  = お店の店頭（お客さんが直接見られる場所）
 * storage フォルダ = 倉庫（スタッフしか入れない保管場所）
 *
 * ユーザーがアップロードした画像は、まず倉庫（storage）に保存されます。
 * でもそのままだとお客さん（ブラウザ）から見えません。
 * だから「倉庫から店頭に通じる近道（リンク）」を作る必要があります。
 * これが次に説明する「storageリンク」です。
 *
 * 【storageフォルダの中身】
 *
 * storage/
 * ├── app/
 * │   ├── public/    ← ユーザーがアップロードしたファイルを保存する場所
 * │   └── private/   ← 非公開ファイル用（今回は使わない）
 * ├── framework/     ← Laravelが自動的に使う作業スペース（触らない）
 * └── logs/          ← エラーログ（触らない）
 *
 * 今回使うのは storage/app/public/ の中だけです。
 */

/**
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * ✅ 【2. storageリンクを作る（シンボリックリンク）】
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 *
 * storage（ストレージ）リンク = 「ショートカット」のようなもの
 *
 * シンボリックリンク（シンボリックリンク）= 別の場所にある本物のファイルへの近道
 *
 * 【なぜ必要か】
 *
 * ブラウザは「laravel-practice/public/」の中しか見られません。
 * でもファイルは「laravel-practice/storage/app/public/」に保存されています。
 *
 * だから：
 * public/storage → storage/app/public/ への近道を作る
 *
 * これで「public/storage/画像名.jpg」というURLでアクセスできるようになります。
 *
 * 【たとえ話：図書館の「出張コーナー」】
 *
 * 本館（storage）は建物の奥にあって、直接入れない。
 * でも「出張コーナー（public/storage）」を作ると、
 * お客さん（ブラウザ）が本館の本を見られるようになる。
 *
 * 【storageリンクを作るコマンド】
 */

// 【ターミナルで実行してください】以下のコマンドをコピーして実行してください：
// docker compose exec php php artisan storage:link

/**
 * 実行すると以下のメッセージが出ます：
 *
 * The [public/storage] link has been connected to [storage/app/public].
 * The links have been created.
 *
 * 意味：「public/storage と storage/app/public がつながりました」
 *
 * 【確認方法】
 *
 * laravel-practice/public/ の中に「storage」というフォルダが
 * 作成されていれば成功です。
 * （アイコンが少し違う見た目になっています = ショートカットの印）
 *
 * ⚠️ 注意点：
 * このコマンドは1回だけ実行すれば大丈夫です。
 * Dockerを再起動しても消えません。
 * 「すでに存在します」というエラーが出ても問題ありません。
 */

/**
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * ✅ 【3. .envのファイルシステム設定を確認する】
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 *
 * .env（ドットイーエヌブイ）= Laravelの設定ファイル
 *
 * 【FILESYSTEM_DISK という設定項目】
 *
 * .env の中に以下の行があります：
 *
 * FILESYSTEM_DISK=local
 *
 * これは「ファイルをどこに保存するか」を決める設定です。
 *
 * | 設定値  | 意味                                       |
 * |--------|-------------------------------------------|
 * | local  | storage/app/ に保存（ブラウザから見えない） |
 * | public | storage/app/public/ に保存（見える）       |
 * | s3     | Amazon S3（クラウドストレージ）に保存       |
 *
 * 今回は「public」に変更します。
 * これにより Storage::put() などで保存したファイルが
 * ブラウザからアクセスできるようになります。
 *
 * 【.env の変更箇所】
 *
 * 変更前：FILESYSTEM_DISK=local
 * 変更後：FILESYSTEM_DISK=public
 *
 * ⚠️ 注意点：
 * .env を変更したら必ずキャッシュをクリアしてください。
 * （後の手順で案内します）
 */

/**
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * ✅ 【4. ファイルがブラウザに届くまでの流れ（まとめ）】
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 *
 * 【アップロード時の流れ】
 *
 * ① ユーザーがフォームで画像を選択して送信
 * ② Laravelが画像を受け取る（$request->file('image')）
 * ③ storage/app/public/expenses/ に保存される
 * ④ ファイルパスをデータベースに記録する
 *    例：'expenses/abc123.jpg'
 *
 * 【表示時の流れ】
 *
 * ① データベースからファイルパスを取り出す
 * ② Storage::url('expenses/abc123.jpg') でURLに変換
 * ③ 変換後のURL：http://localhost:8080/storage/expenses/abc123.jpg
 * ④ ブラウザがそのURLの画像を表示する
 *
 * ↑ public/storage（リンク）→ storage/app/public/ という近道を通って表示される
 *
 * 【整理】
 *
 * 保存場所：storage/app/public/expenses/ファイル名.jpg（本物）
 * アクセスURL：/storage/expenses/ファイル名.jpg（近道経由）
 */

